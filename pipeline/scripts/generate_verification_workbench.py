def build_workbench_page(workbench: dict[str, Any]) -> str:
    queue_rows = []
    for queue in workbench["queues"]:
        queue_rows.append(
            f"| [{md_escape(queue['title'])}]({queue['site_path']}) | `{queue['record_count']}` | `{md_escape(queue['path'])}` |"
        )
    status_rows = [
        f"| `{md_escape(status)}` | `{count}` |"
        for status, count in workbench["summary"]["equation_status_counts"].items()
    ]
    source_rows = [
        f"| `{md_escape(source_id)}` | `{count}` |"
        for source_id, count in workbench["summary"]["figure_source_counts"].items()
    ]
    return f"""---
title: Canonical Verification Workbench
description: Source-linked queues for equation, figure, and patent verification.
---

{GENERATED_NOTE}

## Canonical Verification Workbench

This page gathers the review queues that move the archive from broad generated coverage toward defensible canonical scholarship. It is intentionally conservative: queue membership means the item is important enough to check, not that it has passed.

<div class="codex-signal">
  <div><strong>{workbench['summary']['equation_records']} equations</strong><p>Canonical candidates with OCR line anchors or line-anchor repair tasks.</p></div>
  <div><strong>{workbench['summary']['figure_records']} figures</strong><p>Scan-derived crops awaiting page, crop, and label review.</p></div>
  <div><strong>{workbench['summary']['patent_records']} patents</strong><p>Seeded authority checks for selected Steinmetz patents.</p></div>
</div>

## Queues

| Queue | Records | Data |
| --- | ---: | --- |
{chr(10).join(queue_rows)}

## Equation Status Counts

| Status | Count |
| --- | ---: |
{chr(10).join(status_rows)}

## Figure Source Counts

| Source | Figures |
| --- | ---: |
{chr(10).join(source_rows)}

## Publication Rule

No equation, figure, patent mechanism, or interpretive bridge should be described as definitive until the relevant queue record has passed source-scan review and has a reviewer note attached.
"""


def snippet_block(snippet: dict[str, Any]) -> str:
    line_range = snippet.get("range", {})
    chapter = snippet.get("chapter_title")
    label = f"lines {line_range.get('start')}-{line_range.get('end')}"
    if chapter:
        label = f"{chapter}, {label}"
    if snippet.get("status") != "ocr_snippet_extracted":
        return f"""<details>
  <summary>{html_escape(label)}: {html_escape(snippet.get('status'))}</summary>

Line count: `{snippet.get('line_count', 'unknown')}`.

</details>"""
    lines = "\n".join(f"{line['line']}: {line['text']}" for line in snippet.get("lines", []))
    return f"""<details>
  <summary>OCR context for {html_escape(label)}</summary>

```text
{lines}
```

</details>"""


def build_equation_page(queue: dict[str, Any]) -> str:
    sections = []
    for record in queue["records"]:
        links = record["links"]
        link_bits = [
            f"[Equation page]({links.get('equation_page')})",
            f"[Source overview]({links.get('source_overview')})",
        ]
        for index, href in enumerate(links.get("source_texts", []), start=1):
            link_bits.append(f"[Source text {index}]({href})")
        for index, href in enumerate(links.get("chapter_workbenches", []), start=1):
            link_bits.append(f"[Workbench {index}]({href})")
        if links.get("external_source"):
            link_bits.append(f"[External source]({links['external_source']})")
        snippets = "\n\n".join(snippet_block(snippet) for snippet in record["ocr_snippets"])
        if not snippets:
            snippets = "No OCR line range is attached yet. The next review action is to add an exact source line anchor."
        actions = "\n".join(f"- {md_escape(action)}" for action in record["review_actions"])
        chapters = ", ".join(ref.get("chapter_title", "") for ref in record.get("chapter_refs", []))
        if not chapters:
            chapters = "needs chapter routing"
        sections.append(
            f"""### {md_escape(record['title'])}

<span class="source-badge">{html_escape(record['verification_status'])}</span>

| Field | Value |
| --- | --- |
| Equation ID | `{md_escape(record['id'])}` |
| Source | {md_escape(record['source_title'])} |
| Chapter | {md_escape(chapters)} |
| Source location | {md_escape(record['source_location'])} |
| Original form | `{md_escape(record['original_form'])}` |
| Modern form | `{md_escape(record['modern_form'])}` |
| Candidate status | {md_escape(record['candidate_status'])} |
| Links | {' - '.join(link_bits)} |

{snippets}

Review actions:

{actions}
"""
        )
    return f"""---
title: Equation Verification Queue
description: OCR line snippets and scan-check actions for promoted equation candidates.
---

{GENERATED_NOTE}

## Equation Verification Queue

The records below are the first canonical equation candidates with their current OCR anchors. They remain candidates until the exact printed equation, variables, signs, primes, subscripts, and surrounding definitions are checked against the scan.

{chr(10).join(sections)}
"""


