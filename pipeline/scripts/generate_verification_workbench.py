#!/usr/bin/env python3
"""Generate source-linked verification queues for canonical archive work.

The archive has broad generated coverage, but definitive scholarship needs a
more concrete next layer: exact scan checks, OCR line anchors, and review tasks
for promoted equations, figures, and patents. This script turns existing
canonical candidates into reviewable workbench records without claiming that
the records are verified.
"""

from __future__ import annotations

import argparse
import json
import re
from collections import defaultdict
from datetime import date
from pathlib import Path
from typing import Any


BASE_URL = ""
GENERATED_NOTE = "{/* Generated by pipeline/scripts/generate_verification_workbench.py. Do not edit by hand. */}"

ROMAN_NUMERALS = {
    "i": 1,
    "ii": 2,
    "iii": 3,
    "iv": 4,
    "v": 5,
    "vi": 6,
    "vii": 7,
    "viii": 8,
    "ix": 9,
    "x": 10,
    "xi": 11,
    "xii": 12,
    "xiii": 13,
    "xiv": 14,
    "xv": 15,
    "xvi": 16,
    "xvii": 17,
    "xviii": 18,
    "xix": 19,
    "xx": 20,
}


def load_json(path: Path, default: Any) -> Any:
    if not path.exists():
        return default
    return json.loads(path.read_text(encoding="utf-8"))


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def md_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace("|", "\\|")
        .replace("\n", " ")
    )


def html_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def source_lookup(root: Path) -> dict[str, dict[str, Any]]:
    catalog = load_json(root / "sources" / "source_catalog.json", [])
    if not isinstance(catalog, list):
        return {}
    return {str(source.get("source_id")): source for source in catalog if source.get("source_id")}


def chapter_lookup(root: Path) -> dict[str, list[dict[str, Any]]]:
    atlas = load_json(root / "processed" / "chapter_atlas.json", {})
    records = atlas.get("records", []) if isinstance(atlas, dict) else []
    grouped: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for record in records if isinstance(records, list) else []:
        grouped[str(record.get("source_id"))].append(record)
    for source_id in grouped:
        grouped[source_id].sort(key=lambda item: (int(item.get("sequence") or 0), str(item.get("id"))))
    return dict(grouped)


def parse_chapter_number(location: str, prefix: str) -> int | None:
    pattern = rf"{prefix}\s+([ivxlcdm]+|\d+)"
    match = re.search(pattern, location, flags=re.IGNORECASE)
    if not match:
        return None
    raw = match.group(1).lower()
    if raw.isdigit():
        return int(raw)
    return ROMAN_NUMERALS.get(raw)


def line_anchors(location: str) -> list[dict[str, Any]]:
    anchors = []
    current_kind: str | None = None
    current_number: int | None = None
    previous_end = 0
    for match in re.finditer(r"(\d+)\s*-\s*(\d+)", location, flags=re.IGNORECASE):
        segment = location[previous_end : match.start()]
        section_matches = re.findall(r"(chapter|lecture)\s+([ivxlcdm]+|\d+)", segment, flags=re.IGNORECASE)
        if section_matches:
            current_kind = section_matches[-1][0].lower()
            raw = section_matches[-1][1].lower()
            current_number = int(raw) if raw.isdigit() else ROMAN_NUMERALS.get(raw)
        start = int(match.group(1))
        end = int(match.group(2))
        if end < start:
            start, end = end, start
        anchors.append(
            {
                "start": start,
                "end": end,
                "section_kind": current_kind,
                "section_number": current_number,
            }
        )
        previous_end = match.end()
    return anchors


def expected_text_filename(location: str, anchor: dict[str, Any] | None = None) -> str | None:
    if anchor and anchor.get("section_kind") and anchor.get("section_number"):
        return f"{anchor['section_kind']}-{int(anchor['section_number']):02d}.md"
    lecture = parse_chapter_number(location, "lecture")
    if lecture is not None:
        return f"lecture-{lecture:02d}.md"
    chapter = parse_chapter_number(location, "chapter")
    if chapter is not None:
        return f"chapter-{chapter:02d}.md"
    return None


def find_chapter_record(
    chapters_by_source: dict[str, list[dict[str, Any]]],
    source_id: str,
    location: str,
    anchor: dict[str, Any] | None = None,
) -> dict[str, Any] | None:
    expected = expected_text_filename(location, anchor)
    source_chapters = chapters_by_source.get(source_id, [])
    if expected:
        for record in source_chapters:
            text_path = str(record.get("text_path", ""))
            if Path(text_path).name.lower() == expected.lower():
                return record
    if source_chapters:
        return source_chapters[0]
    return None


def page_slug_from_text_path(text_path: str) -> str:
    return Path(text_path).stem


def source_text_link(source_id: str, text_path: str) -> str:
    return f"{BASE_URL}/source-texts/{source_id}/{page_slug_from_text_path(text_path)}/"


def workbench_link(source_id: str, text_path: str) -> str:
    return f"{BASE_URL}/chapter-workbench/{source_id}/{page_slug_from_text_path(text_path)}/"


def source_overview_link(source: dict[str, Any]) -> str:
    site_path = source.get("site_path")
    if site_path:
        return f"{BASE_URL}{site_path}"
    source_id = source.get("source_id", "")
    return f"{BASE_URL}/sources/{source_id}/"


def external_source_link(source: dict[str, Any]) -> str | None:
    archive_id = source.get("internet_archive_id")
    if archive_id:
        return f"https://archive.org/details/{archive_id}"
    local_raw = source.get("local_raw_file")
    if local_raw:
        return f"https://github.com/TRUEMODELOFTHEWORLD/Steinmetz-Decoded/blob/main/{local_raw}"
    return None


def extract_snippet(root: Path, text_path: str, line_range: dict[str, int], context: int = 2) -> dict[str, Any]:
    full_path = root / text_path
    if not full_path.exists():
        return {
            "range": line_range,
            "text_path": text_path,
            "status": "missing_text_file",
            "lines": [],
        }
    lines = full_path.read_text(encoding="utf-8", errors="replace").splitlines()
    start = max(1, int(line_range["start"]) - context)
    end = min(len(lines), int(line_range["end"]) + context)
    if int(line_range["start"]) > len(lines):
        return {
            "range": line_range,
            "text_path": text_path,
            "line_count": len(lines),
            "status": "line_range_out_of_bounds",
            "lines": [],
        }
    return {
        "range": line_range,
        "text_path": text_path,
        "line_count": len(lines),
        "context_start": start,
        "context_end": end,
        "status": "ocr_snippet_extracted",
        "lines": [{"line": number, "text": lines[number - 1]} for number in range(start, end + 1)],
    }


def build_equation_queue(root: Path) -> dict[str, Any]:
    equations = load_json(root / "processed" / "canonical_equations.json", {}).get("records", [])
    sources = source_lookup(root)
    chapters_by_source = chapter_lookup(root)
    records = []

    for equation in equations if isinstance(equations, list) else []:
        source_id = str(equation.get("source_id"))
        location = str(equation.get("source_ref", {}).get("location", ""))
        source = sources.get(source_id, {"source_id": source_id})
        anchors = line_anchors(location)
        snippets = []
        links = {
            "equation_page": f"{BASE_URL}{equation.get('site_path', '')}",
            "source_overview": source_overview_link(source),
            "external_source": external_source_link(source),
        }
        chapter_refs: list[dict[str, Any]] = []
        source_text_links: list[str] = []
        workbench_links: list[str] = []
        for anchor in anchors:
            chapter = find_chapter_record(chapters_by_source, source_id, location, anchor)
            if not chapter or not chapter.get("text_path"):
                snippets.append(
                    {
                        "range": {"start": anchor["start"], "end": anchor["end"]},
                        "section_kind": anchor.get("section_kind"),
                        "section_number": anchor.get("section_number"),
                        "status": "missing_chapter_route",
                        "lines": [],
                    }
                )
                continue
            text_path = str(chapter["text_path"])
            chapter_ref = {
                "chapter_id": chapter.get("id"),
                "chapter_title": chapter.get("title"),
                "text_path": text_path,
                "source_text": source_text_link(source_id, text_path),
                "chapter_workbench": workbench_link(source_id, text_path),
            }
            if chapter_ref not in chapter_refs:
                chapter_refs.append(chapter_ref)
            if chapter_ref["source_text"] not in source_text_links:
                source_text_links.append(chapter_ref["source_text"])
            if chapter_ref["chapter_workbench"] not in workbench_links:
                workbench_links.append(chapter_ref["chapter_workbench"])
            snippet = extract_snippet(root, text_path, {"start": anchor["start"], "end": anchor["end"]})
            snippet["section_kind"] = anchor.get("section_kind")
            snippet["section_number"] = anchor.get("section_number")
            snippet["chapter_id"] = chapter.get("id")
            snippet["chapter_title"] = chapter.get("title")
            snippets.append(snippet)
        if source_text_links:
            links["source_texts"] = source_text_links
        if workbench_links:
            links["chapter_workbenches"] = workbench_links

        needs_line_anchor = not anchors
        snippet_statuses = {snippet["status"] for snippet in snippets}
        if needs_line_anchor:
            review_status = "needs_line_anchor"
        elif (
            "line_range_out_of_bounds" in snippet_statuses
            or "missing_text_file" in snippet_statuses
            or "missing_chapter_route" in snippet_statuses
        ):
            review_status = "needs_line_anchor_repair"
        else:
            review_status = "ready_for_scan_check"

        records.append(
            {
                "id": equation.get("id"),
                "title": equation.get("title"),
                "source_id": source_id,
                "source_title": equation.get("source_title"),
                "chapter_refs": chapter_refs,
                "chapter_id": chapter_refs[0]["chapter_id"] if chapter_refs else None,
                "chapter_title": chapter_refs[0]["chapter_title"] if chapter_refs else None,
                "original_form": equation.get("original_form"),
                "modern_form": equation.get("modern_form"),
                "candidate_status": equation.get("status"),
                "source_location": location,
                "line_ranges": [{"start": anchor["start"], "end": anchor["end"]} for anchor in anchors],
                "line_anchors": anchors,
                "needs_line_anchor": needs_line_anchor,
                "ocr_snippets": snippets,
                "verification_status": review_status,
                "links": links,
                "review_actions": [
                    "Open the source scan and confirm the printed equation typography.",
                    "Check the OCR snippet against the scan and repair symbols, primes, subscripts, and signs.",
                    "Record whether Steinmetz is using maximum, effective, instantaneous, or symbolic quantities.",
                    "Update the equation page only after source text and notation checks agree.",
                ],
                "quality_label": "source-located OCR candidate; not scan-certified",
            }
        )

    status_counts = defaultdict(int)
    source_counts = defaultdict(int)
    for record in records:
        status_counts[record["verification_status"]] += 1
        source_counts[record["source_id"]] += 1

    return {
        "generated_at": date.today().isoformat(),
        "quality_note": "Equation verification queue. OCR snippets are review aids; exact canonical equations require scan verification.",
        "record_count": len(records),
        "status_counts": dict(sorted(status_counts.items())),
        "source_counts": dict(sorted(source_counts.items())),
        "records": records,
    }


def build_figure_queue(root: Path) -> dict[str, Any]:
    ledger = load_json(root / "processed" / "diagram_provenance_ledger.json", {})
    crops = ledger.get("original_crops", []) if isinstance(ledger, dict) else []
    sources = source_lookup(root)
    records = []

    for crop in crops if isinstance(crops, list) else []:
        source_id = str(crop.get("source_id"))
        source = sources.get(source_id, {"source_id": source_id})
        records.append(
            {
                "id": crop.get("id"),
                "source_id": source_id,
                "source_title": crop.get("source_title"),
                "status": crop.get("status"),
                "source_location": crop.get("source_location"),
                "source_image": crop.get("source_image"),
                "crop_box_pixels": crop.get("crop_box_pixels"),
                "output_path": crop.get("output_path"),
                "public_url": crop.get("public_url"),
                "sha256": crop.get("sha256"),
                "manifest_path": crop.get("manifest_path"),
                "verification_status": "ready_for_scan_crop_review",
                "links": {
                    "image": crop.get("public_url"),
                    "source_overview": source_overview_link(source),
                    "external_source": external_source_link(source),
                    "diagram_archive": f"{BASE_URL}/diagrams/",
                },
                "review_actions": [
                    "Open the original PDF page render and confirm the crop includes the complete figure.",
                    "Confirm printed page number, PDF page number, and figure label.",
                    "Check image legibility and record whether a cleaned crop or vector redraw is required.",
                    "Keep any interpretive annotations separate from the scan-derived original crop.",
                ],
            }
        )

    source_counts = defaultdict(int)
    for record in records:
        source_counts[record["source_id"]] += 1

    return {
        "generated_at": date.today().isoformat(),
        "quality_note": "Figure verification queue. Original crops are scan-derived assets that need page/crop/label review before canonical promotion.",
        "record_count": len(records),
        "source_counts": dict(sorted(source_counts.items())),
        "records": records,
    }


def build_patent_queue(root: Path) -> dict[str, Any]:
    bridge = load_json(root / "processed" / "patent_theory_bridge.json", {})
    patents = bridge.get("records", []) if isinstance(bridge, dict) else []
    records = []

    for patent in patents if isinstance(patents, list) else []:
        number = patent.get("patent_number")
        records.append(
            {
                "patent_number": number,
                "title": patent.get("title"),
                "publication_date": patent.get("publication_date"),
                "patent_url": patent.get("patent_url"),
                "pdf_url": patent.get("pdf_url"),
                "domain_tags": patent.get("domain_tags", []),
                "concept_links": patent.get("concept_links", []),
                "diagram_targets": patent.get("diagram_targets", []),
                "research_status": patent.get("research_status"),
                "verification_status": "needs_authority_pdf_claim_and_drawing_extraction",
                "links": {
                    "patent_page": patent.get("patent_url"),
                    "authority_pdf": patent.get("pdf_url"),
                    "patent_register": f"{BASE_URL}/sources/steinmetz-patents/",
                    "patent_theory_bridge": f"{BASE_URL}/roadmap/patent-to-theory-bridge/",
                },
                "review_actions": [
                    "Download or open the authority PDF and record filing/publication metadata.",
                    "Extract claims, drawings, figure labels, and assignee/inventor details.",
                    "Summarize the patent in strict claim-language terms before adding interpretation.",
                    "Bridge only explicit patent mechanisms to Steinmetz source-text concepts.",
                ],
            }
        )

    concept_counts = defaultdict(int)
    for record in records:
        for concept in record["concept_links"]:
            concept_counts[str(concept)] += 1

    return {
        "generated_at": date.today().isoformat(),
        "quality_note": "Patent verification queue. The current patent set is seeded from selected public listings and is not a complete Steinmetz patent bibliography.",
        "record_count": len(records),
        "scope_warning": bridge.get("scope_warning"),
        "concept_counts": dict(sorted(concept_counts.items())),
        "records": records,
    }


