def load_json(path: Path, default: Any) -> Any:
    if not path.exists():
        return default
    return json.loads(path.read_text(encoding="utf-8"))


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def slugify(value: str) -> str:
    value = value.lower()
    value = re.sub(r"[^a-z0-9]+", "-", value)
    value = value.strip("-")
    return value or "untitled"


def yaml_quote(value: Any) -> str:
    text = "" if value is None else str(value)
    text = text.replace("\\", "\\\\").replace('"', '\\"')
    return f'"{text}"'


def md_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace("{", "&#123;")
        .replace("}", "&#125;")
        .replace("$", "&#36;")
        .replace("|", "\\|")
        .replace("\n", " ")
    )


def focused_reader_url(url: Any, term: Any) -> str:
    value = str(url or "")
    query = str(term or "").strip()
    if not value or not query:
        return value
    base = value.split("#", 1)[0]
    separator = "&" if "?" in base else "?"
    return f"{base}{separator}q={quote(query)}#source-text-reader"


def html_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def normalize_space(text: str) -> str:
    return re.sub(r"\s+", " ", text).strip()


def code_fence(text: str) -> str:
    text = text.rstrip() or "[No snippet captured.]"
    longest = 3
    for match in re.finditer(r"`+", text):
        longest = max(longest, len(match.group(0)) + 1)
    fence = "`" * longest
    return f"{fence}text wrap\n{text}\n{fence}"


def source_page_slug(record: dict[str, Any]) -> str:
    path = Path(str(record.get("text_path") or ""))
    if path.stem:
        return slugify(path.stem)
    return slugify(str(record.get("id") or record.get("title") or "section"))


def chapter_label(record: dict[str, Any]) -> str:
    kind = str(record.get("kind") or "section").replace("-", " ").title()
    number = record.get("number") or record.get("sequence")
    title = str(record.get("title") or "Untitled")
    if number:
        return f"{kind} {number}: {title}"
    return f"{kind}: {title}"


def record_location(record: dict[str, Any]) -> str:
    start = record.get("line_start")
    end = record.get("line_end")
    page_start = record.get("pdf_page_start")
    page_end = record.get("pdf_page_end")
    parts = []
    if page_start and page_end:
        parts.append(f"PDF pages {page_start}-{page_end}" if page_start != page_end else f"PDF page {page_start}")
    if start and end:
        parts.append(f"lines {start}-{end}")
    return ", ".join(parts) or "processed source text"


def read_chapter_text(root: Path, record: dict[str, Any]) -> str:
    path = root / str(record.get("text_path") or "")
    if not path.exists():
        return ""
    return path.read_text(encoding="utf-8", errors="replace")


def flexible_alias_pattern(alias: str) -> str:
    alias = alias.strip()
    if not alias:
        return r"(?!x)x"
    if alias.lower() == "a.c.":
        return r"(?<![A-Za-z0-9])a\s*\.?\s*c\s*\.?(?![A-Za-z0-9])"
    tokens = re.findall(r"[A-Za-z0-9]+", alias.lower())
    if not tokens:
        return re.escape(alias.lower())
    if len(tokens) == 1:
        body = re.escape(tokens[0])
    else:
        body = r"[\s\-/.]+".join(re.escape(token) for token in tokens)
    return rf"(?<![A-Za-z0-9]){body}(?![A-Za-z0-9])"


def compile_alias_regex(aliases: list[str]) -> re.Pattern[str]:
    patterns = [flexible_alias_pattern(alias) for alias in aliases if alias.strip()]
    patterns = sorted(set(patterns), key=len, reverse=True)
    return re.compile("|".join(patterns) or r"(?!x)x", re.IGNORECASE)


def alias_counts(text: str, aliases: list[str]) -> list[dict[str, Any]]:
    counts = []
    for alias in aliases:
        regex = compile_alias_regex([alias])
        count = len(list(regex.finditer(text)))
        if count:
            counts.append({"alias": alias, "count": count})
    counts.sort(key=lambda item: (-int(item["count"]), str(item["alias"]).lower()))
    return counts


def find_snippets(text: str, matches: list[re.Match[str]], radius: int = 300, limit: int = 3) -> list[str]:
    snippets: list[str] = []
    seen: set[str] = set()
    for match in matches[: max(limit * 4, limit)]:
        start = max(0, match.start() - radius)
        end = min(len(text), match.end() + radius)
        snippet = normalize_space(text[start:end])
        if start > 0:
            snippet = "... " + snippet
        if end < len(text):
            snippet += " ..."
        key = snippet.lower()
        if key not in seen:
            snippets.append(snippet)
            seen.add(key)
        if len(snippets) >= limit:
            break
    return snippets


def load_chapter_records(root: Path, source_catalog: list[dict[str, Any]]) -> list[dict[str, Any]]:
    records: list[dict[str, Any]] = []
    source_titles = {source["source_id"]: source.get("title") for source in source_catalog}
    source_years = {source["source_id"]: source.get("year") for source in source_catalog}
    for source in source_catalog:
        source_id = str(source["source_id"])
        chapters = load_json(root / "processed" / source_id / "chapters.json", [])
        if not isinstance(chapters, list):
            continue
        for record in chapters:
            text = read_chapter_text(root, record)
            if not text:
                continue
            slug = source_page_slug(record)
            record = dict(record)
            record["_text"] = text
            record["_source_title"] = source_titles.get(source_id) or source_id
            record["_year"] = source_years.get(source_id)
            record["_source_text_url"] = f"{BASE_URL}/source-texts/{source_id}/{slug}/"
            record["_workbench_url"] = f"{BASE_URL}/chapter-workbench/{source_id}/{slug}/"
            records.append(record)
    return records


def build_theme_atlas(root: Path) -> dict[str, Any]:
    source_catalog = load_json(root / "sources" / "source_catalog.json", [])
    if not isinstance(source_catalog, list):
        source_catalog = []
    source_titles = {source.get("source_id"): source.get("title") for source in source_catalog}
    chapter_records = load_chapter_records(root, source_catalog)

    themes: list[dict[str, Any]] = []
    for theme_id, theme in THEMES.items():
        regex = compile_alias_regex(theme["aliases"])
        section_hits: list[dict[str, Any]] = []
        source_totals: Counter[str] = Counter()
        source_sections: Counter[str] = Counter()
        total_occurrences = 0
        total_alias_counts: Counter[str] = Counter()

        for record in chapter_records:
            text = str(record.get("_text") or "")
            matches = list(regex.finditer(text))
            if not matches:
                continue
            source_id = str(record.get("source_id") or "")
            count = len(matches)
            aliases_in_section = alias_counts(text, theme["aliases"])
            for alias_count in aliases_in_section:
                total_alias_counts[str(alias_count["alias"])] += int(alias_count["count"])

            total_occurrences += count
            source_totals[source_id] += count
            source_sections[source_id] += 1
            section_hits.append(
                {
                    "source_id": source_id,
                    "source_title": record.get("_source_title"),
                    "year": record.get("_year"),
                    "section_id": record.get("id"),
                    "section_label": chapter_label(record),
                    "section_title": record.get("title"),
                    "kind": record.get("kind"),
                    "sequence": record.get("sequence"),
                    "number": record.get("number"),
                    "location": record_location(record),
                    "status": record.get("status", "candidate"),
                    "word_count": record.get("word_count"),
                    "occurrence_count": count,
                    "top_aliases": aliases_in_section[:8],
                    "source_text_url": record.get("_source_text_url"),
                    "workbench_url": record.get("_workbench_url"),
                    "snippets": find_snippets(text, matches),
                }
            )

        section_hits.sort(key=lambda item: (-int(item["occurrence_count"]), str(item["source_id"]), str(item["section_id"])))
        themes.append(
            {
                "id": theme_id,
                "label": theme["label"],
                "description": theme["description"],
                "aliases": theme["aliases"],
                "modern_prompt": theme["modern_prompt"],
                "interpretive_boundary": theme["interpretive_boundary"],
                "total_occurrences": total_occurrences,
                "matching_source_count": len(source_totals),
                "matching_section_count": len(section_hits),
                "source_totals": [
                    {
                        "source_id": source_id,
                        "source_title": source_titles.get(source_id, source_id),
                        "occurrence_count": source_totals[source_id],
                        "section_count": source_sections[source_id],
                    }
                    for source_id, _ in source_totals.most_common()
                ],
                "alias_totals": [
                    {"alias": alias, "count": count}
                    for alias, count in total_alias_counts.most_common()
                ],
                "section_hits": section_hits,
            }
        )

    themes.sort(key=lambda item: (-int(item["total_occurrences"]), item["label"]))
    return {
        "generated_at": date.today().isoformat(),
        "quality_note": "Generated theme evidence atlas. Counts and snippets are OCR/PDF-text source-location aids, not final claims. Verify exact passages against scans before quotation or interpretation.",
        "person": "Charles Proteus Steinmetz",
        "collection": "steinmetz",
        "source_count": len(source_catalog),
        "section_count": len(chapter_records),
        "total_themes": len(themes),
        "themes": themes,
    }


