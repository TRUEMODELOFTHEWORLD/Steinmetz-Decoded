def write_pages(root: Path, atlas: dict[str, Any]) -> None:
    out_dir = root / "site" / "src" / "content" / "docs" / "theme-evidence"
    if out_dir.exists():
        shutil.rmtree(out_dir)
    write_text(out_dir / "index.mdx", build_index_page(atlas))
    for theme in atlas["themes"]:
        write_text(out_dir / str(theme["id"]) / "index.mdx", build_theme_page(theme))


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--root", type=Path, default=Path(__file__).resolve().parents[2])
    args = parser.parse_args()
    root = args.root.resolve()
    atlas = build_theme_atlas(root)
    write_json(root / "processed" / "theme_evidence_atlas.json", atlas)
    write_pages(root, atlas)
    print(
        f"Generated {atlas['total_themes']} theme evidence pages "
        f"from {atlas['section_count']} processed sections."
    )


if __name__ == "__main__":
    main()
