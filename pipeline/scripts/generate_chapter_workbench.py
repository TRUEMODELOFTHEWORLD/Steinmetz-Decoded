def load_json(path: Path, default: Any) -> Any:
    if not path.exists():
        return default
    return json.loads(path.read_text(encoding="utf-8"))


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


def slugify(value: str) -> str:
    value = value.lower()
    value = re.sub(r"[^a-z0-9]+", "-", value)
    value = value.strip("-")
    return value or "untitled"


def yaml_quote(value: Any) -> str:
    text = "" if value is None else str(value)
    text = text.replace("\\", "\\\\").replace('"', '\\"')
    return f'"{text}"'


def md_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace("{", "&#123;")
        .replace("}", "&#125;")
        .replace("$", "&#36;")
        .replace("|", "\\|")
        .replace("\n", " ")
    )


def html_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def code_fence(text: str) -> str:
    text = text.rstrip() or "[No source excerpt available.]"
    longest = 3
    for match in re.finditer(r"`+", text):
        longest = max(longest, len(match.group(0)) + 1)
    fence = "`" * longest
    return f"{fence}text wrap\n{text}\n{fence}"


def source_page_slug(record: dict[str, Any]) -> str:
    path = Path(str(record.get("text_path") or ""))
    if path.stem:
        return slugify(path.stem)
    return slugify(str(record.get("id") or record.get("title") or "section"))


def chapter_label(record: dict[str, Any]) -> str:
    kind = str(record.get("kind") or "section").replace("-", " ").title()
    number = record.get("number") or record.get("sequence")
    title = str(record.get("title") or "Untitled")
    if number:
        return f"{kind} {number}: {title}"
    return f"{kind}: {title}"


def record_location(record: dict[str, Any]) -> str:
    start = record.get("line_start")
    end = record.get("line_end")
    page_start = record.get("pdf_page_start")
    page_end = record.get("pdf_page_end")
    parts = []
    if page_start and page_end:
        parts.append(f"PDF pages {page_start}-{page_end}" if page_start != page_end else f"PDF page {page_start}")
    if start and end:
        parts.append(f"lines {start}-{end}")
    return ", ".join(parts) or "processed source text"


def read_chapter_text(root: Path, record: dict[str, Any]) -> str:
    path = root / str(record.get("text_path") or "")
    if not path.exists():
        return ""
    return path.read_text(encoding="utf-8", errors="replace")


def words(text: str) -> list[str]:
    return re.findall(r"[A-Za-z][A-Za-z'-]*", text)


def opening_excerpt(text: str, max_words: int = 135) -> str:
    tokens = text.strip().split()
    return " ".join(tokens[:max_words])


def normalize_space(text: str) -> str:
    return re.sub(r"\s+", " ", text).strip()


def find_snippet(text: str, patterns: list[str], radius: int = 280) -> str:
    lower = text.lower()
    best_index = -1
    best_pattern = ""
    for pattern in patterns:
        index = lower.find(pattern.lower().strip())
        if index >= 0 and (best_index < 0 or index < best_index):
            best_index = index
            best_pattern = pattern
    if best_index < 0:
        return ""
    start = max(0, best_index - radius)
    end = min(len(text), best_index + len(best_pattern) + radius)
    snippet = normalize_space(text[start:end])
    if start:
        snippet = "... " + snippet
    if end < len(text):
        snippet += " ..."
    return snippet


def compact_ref(record: dict[str, Any]) -> tuple[int | None, int | None]:
    source_ref = record.get("source_ref")
    if not isinstance(source_ref, dict):
        return None, None
    start = source_ref.get("line_start")
    end = source_ref.get("line_end") or start
    return int(start) if start else None, int(end) if end else None


def overlaps_chapter(record: dict[str, Any], chapter: dict[str, Any]) -> bool:
    chapter_start = chapter.get("line_start")
    chapter_end = chapter.get("line_end")
    start, end = compact_ref(record)
    if not chapter_start or not chapter_end or not start:
        return False
    return int(start) <= int(chapter_end) and int(end or start) >= int(chapter_start)


def term_hit_count(text: str, term: str) -> int:
    if not term:
        return 0
    lower = f" {text.lower()} "
    plain = term.lower().replace("-", " ")
    dashed = term.lower()
    return lower.count(plain) + (0 if dashed == plain else lower.count(dashed))


def collect_term_hits(text: str, records: list[dict[str, Any]], limit: int = 12) -> list[dict[str, Any]]:
    hits: list[dict[str, Any]] = []
    seen: set[str] = set()
    for record in records:
        label = str(record.get("label") or record.get("term") or record.get("id") or "")
        key = slugify(label)
        if not label or key in seen:
            continue
        count = term_hit_count(text, label)
        if not count:
            alt = str(record.get("id") or "").replace("-", " ")
            count = term_hit_count(text, alt)
        if count:
            seen.add(key)
            hits.append(
                {
                    "id": record.get("id"),
                    "label": label,
                    "count": count,
                    "status": record.get("status", "candidate"),
                }
            )
    hits.sort(key=lambda item: (-int(item["count"]), str(item["label"]).lower()))
    return hits[:limit]


def short_value(value: Any, max_len: int = 180) -> str:
    text = normalize_space(str(value or ""))
    if len(text) <= max_len:
        return text
    return text[: max_len - 3].rstrip() + "..."


def source_links(source: dict[str, Any], record: dict[str, Any]) -> dict[str, str]:
    source_id = str(source["source_id"])
    slug = source_page_slug(record)
    text_path = str(record.get("text_path") or "")
    raw_file = source.get("local_raw_file")
    ia_id = source.get("internet_archive_id")
    links = {
        "source_text": f"{BASE_URL}/source-texts/{source_id}/{slug}/",
        "source_index": f"{BASE_URL}/source-texts/{source_id}/",
        "curated_source": f"{BASE_URL}{source.get('site_path') or f'/sources/{source_id}/'}",
        "github_text": f"{GITHUB_BASE}/{text_path}" if text_path else "",
        "asset": f"{BASE_URL}/source-texts-data/{source_id}/{slug}.txt",
        "archive": f"https://archive.org/details/{ia_id}" if ia_id else "",
        "raw_pdf": f"{GITHUB_BASE}/{raw_file}" if raw_file else "",
    }
    return links


def build_record(
    root: Path,
    source: dict[str, Any],
    chapter: dict[str, Any],
    atlas_record: dict[str, Any],
    concepts: list[dict[str, Any]],
    glossary: list[dict[str, Any]],
    equations: list[dict[str, Any]],
    figures: list[dict[str, Any]],
    quotes: list[dict[str, Any]],
) -> dict[str, Any]:
    text = read_chapter_text(root, chapter)
    chapter_equations = [record for record in equations if overlaps_chapter(record, chapter)]
    chapter_figures = [record for record in figures if overlaps_chapter(record, chapter)]
    chapter_quotes = [record for record in quotes if overlaps_chapter(record, chapter)]
    theme_counts = atlas_record.get("theme_counts") if isinstance(atlas_record, dict) else {}
    if not isinstance(theme_counts, dict):
        theme_counts = {}
    top_themes = list(atlas_record.get("top_themes") or []) if isinstance(atlas_record, dict) else []
    if not top_themes:
        top_themes = [theme for theme, _count in Counter(theme_counts).most_common(5)]

    snippets = []
    for theme in top_themes[:5]:
        snippet = find_snippet(text, THEME_PATTERNS.get(theme, []))
        if snippet:
            snippets.append({"theme": theme, "label": THEME_LABELS.get(theme, theme), "snippet": snippet})

    return {
        "id": chapter.get("id"),
        "source_id": source["source_id"],
        "source_title": source.get("title"),
        "year": source.get("year"),
        "kind": chapter.get("kind"),
        "sequence": chapter.get("sequence"),
        "title": chapter.get("title"),
        "label": chapter_label(chapter),
        "slug": source_page_slug(chapter),
        "location": record_location(chapter),
        "line_start": chapter.get("line_start"),
        "line_end": chapter.get("line_end"),
        "status": chapter.get("status", "candidate"),
        "word_count": len(words(text)),
        "top_themes": top_themes[:6],
        "theme_counts": theme_counts,
        "concept_hits": collect_term_hits(text, concepts, 12),
        "glossary_hits": collect_term_hits(text, glossary, 12),
        "equation_count": len(chapter_equations),
        "figure_count": len(chapter_figures),
        "quote_count": len(chapter_quotes),
        "equations": chapter_equations[:8],
        "figures": chapter_figures[:8],
        "quotes": chapter_quotes[:6],
        "opening_excerpt": opening_excerpt(text),
        "theme_snippets": snippets[:4],
        "links": source_links(source, chapter),
    }


def build_theme_badges(record: dict[str, Any]) -> str:
    badges = []
    theme_counts = record.get("theme_counts") or {}
    for theme in record.get("top_themes") or []:
        label = THEME_LABELS.get(theme, theme)
        count = theme_counts.get(theme, 0)
        href = THEME_TO_CONCEPT.get(theme)
        text = f"{html_escape(label)}: {html_escape(count)}"
        if href:
            badges.append(f'<a href="{BASE_URL}/{href}/">{text}</a>')
        else:
            badges.append(f"<span>{text}</span>")
    return "\n  ".join(badges) or "<span>no theme hits</span>"


def table_rows_for_terms(terms: list[dict[str, Any]]) -> str:
    if not terms:
        return "| No chapter-local term hits yet | - | - |\n"
    rows = []
    for term in terms:
        rows.append(
            f"| {md_escape(term.get('label'))} | {md_escape(term.get('count'))} | {md_escape(term.get('status'))} |"
        )
    return "\n".join(rows)


def table_rows_for_candidates(records: list[dict[str, Any]], label_field: str) -> str:
    if not records:
        return "| No chapter-local candidates yet | - | - |\n"
    rows = []
    for record in records:
        source_ref = record.get("source_ref") if isinstance(record.get("source_ref"), dict) else {}
        location = "-"
        if source_ref:
            start = source_ref.get("line_start")
            end = source_ref.get("line_end") or start
            location = f"lines {start}-{end}" if start and end and start != end else f"line {start}" if start else "-"
        rows.append(
            f"| `{md_escape(record.get('id'))}` | {md_escape(short_value(record.get(label_field)))} | {md_escape(location)} |"
        )
    return "\n".join(rows)


def build_prompt_blocks(record: dict[str, Any]) -> str:
    modern = []
    interpretive = []
    for theme in record.get("top_themes") or []:
        if theme in MODERN_PROMPTS:
            modern.append(f"- **{md_escape(THEME_LABELS.get(theme, theme))}:** {md_escape(MODERN_PROMPTS[theme])}")
        if theme in INTERPRETIVE_PROMPTS:
            interpretive.append(
                f"- **{md_escape(THEME_LABELS.get(theme, theme))}:** {md_escape(INTERPRETIVE_PROMPTS[theme])}"
            )
    if not modern:
        modern.append("- Use the source text first, then add modern translation only after a verified passage is promoted.")
    if not interpretive:
        interpretive.append("- No special ether-field prompt was generated for the current top themes. Add one only after source review.")

    return f"""<section data-layer="modern">

## Modern Engineering Reading Prompts

{chr(10).join(modern[:6])}

</section>

<section data-layer="interpretive">

## Ether-Field Interpretive Boundary

{chr(10).join(interpretive[:6])}

</section>
"""


def build_snippet_blocks(record: dict[str, Any]) -> str:
    blocks = []
    for snippet in record.get("theme_snippets") or []:
        blocks.append(
            f"""### {md_escape(snippet.get('label'))}

{code_fence(str(snippet.get("snippet") or ""))}
"""
        )
    if not blocks:
        return "No theme snippets were generated. Open the full source text for this section.\n"
    return "\n".join(blocks)


