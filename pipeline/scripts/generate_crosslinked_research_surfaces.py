def build_source_meta(equation_atlas: dict[str, Any], figure_atlas: dict[str, Any]) -> dict[str, dict[str, Any]]:
    sources: dict[str, dict[str, Any]] = {}
    for source in equation_atlas.get("source_summaries") or []:
        source_id = str(source.get("source_id"))
        sources[source_id] = {
            "source_id": source_id,
            "title": source.get("title"),
            "year": source.get("year"),
        }
    for source in figure_atlas.get("source_summaries") or []:
        source_id = str(source.get("source_id"))
        sources.setdefault(source_id, {"source_id": source_id})
        sources[source_id]["title"] = sources[source_id].get("title") or source.get("title")
        sources[source_id]["year"] = sources[source_id].get("year") or source.get("year")
    return sources


