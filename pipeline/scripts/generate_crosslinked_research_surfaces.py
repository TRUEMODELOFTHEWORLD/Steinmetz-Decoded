def formula_rows(records: list[dict[str, Any]], limit: int = 12) -> str:
    rows = []
    sorted_records = sorted(records, key=lambda item: (-int(item.get("quality_score") or 0), str(item.get("id"))))
    for record in sorted_records[:limit]:
        links = record.get("links") or {}
        source_text = links.get("source_text") or links.get("source_text_index") or "#"
        workbench = links.get("workbench") or links.get("workbench_index") or "#"
        rows.append(
            "<tr>"
            f"<td><code>{html_escape(record.get('id'))}</code><br/><small>{html_escape(record.get('quality_band'))}</small></td>"
            f"<td>{html_escape(record.get('family'))}</td>"
            f"<td><code>{html_escape(clean(record.get('original_form'), 180))}</code></td>"
            f"<td><a href=\"{html_escape(source_text)}\">source</a><br/><a href=\"{html_escape(workbench)}\">workbench</a></td>"
            "</tr>"
        )
    if not rows:
        rows.append('<tr><td colspan="4">No formula candidates routed here yet.</td></tr>')
    return "\n".join(rows)


def figure_rows(records: list[dict[str, Any]], limit: int = 14) -> str:
    rows = []
    sorted_records = sorted(records, key=lambda item: (int(item.get("line_start") or 9999999), int(item.get("figure_number") or 9999999)))
    for record in sorted_records[:limit]:
        links = record.get("links") or {}
        source_text = links.get("source_text") or links.get("source_text_index") or "#"
        workbench = links.get("workbench") or links.get("workbench_index") or "#"
        fig_no = record.get("figure_number")
        figure_label = f"Fig. {fig_no}" if fig_no is not None else "Figure candidate"
        rows.append(
            "<tr>"
            f"<td><code>{html_escape(record.get('id'))}</code><br/><small>{html_escape(figure_label)}</small></td>"
            f"<td>{html_escape(clean(record.get('caption'), 220))}</td>"
            f"<td>{html_escape(record.get('section_label'))}</td>"
            f"<td><a href=\"{html_escape(source_text)}\">source</a><br/><a href=\"{html_escape(workbench)}\">workbench</a></td>"
            "</tr>"
        )
    if not rows:
        rows.append('<tr><td colspan="4">No figure candidates routed here yet.</td></tr>')
    return "\n".join(rows)


