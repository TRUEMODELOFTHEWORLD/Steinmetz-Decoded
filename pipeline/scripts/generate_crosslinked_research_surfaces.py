def build_concept_bridge(
    dossier: dict[str, Any],
    equations_by_source: dict[str, list[dict[str, Any]]],
    figures_by_source: dict[str, list[dict[str, Any]]],
    visuals: list[dict[str, Any]],
) -> dict[str, Any]:
    page_slug = str(dossier.get("page_slug"))
    rule = CONCEPT_RULES.get(page_slug, {"families": [], "keywords": [page_slug.replace("-", " ")], "visual_terms": [page_slug]})
    source_ids = [str(source.get("source_id")) for source in dossier.get("sources") or [] if source.get("source_id")]
    keyword_terms = list(rule.get("keywords") or [])
    families = set(rule.get("families") or [])

    equation_records: list[dict[str, Any]] = []
    for source_id in source_ids:
        for record in equations_by_source.get(source_id, []):
            if str(record.get("family")) in families or text_matches_keywords(record, keyword_terms):
                equation_records.append(record)

    figure_records: list[dict[str, Any]] = []
    for source_id in source_ids:
        for record in figures_by_source.get(source_id, []):
            if text_matches_keywords(record, keyword_terms):
                figure_records.append(record)
    if not figure_records:
        for source_id in source_ids[:4]:
            figure_records.extend(figures_by_source.get(source_id, [])[:3])

    visual_terms = list(rule.get("visual_terms") or [])
    visual_records = [
        record
        for record in visuals
        if visual_matches_terms(record, visual_terms if visual_terms else keyword_terms)
    ]
    formula_source_counts = Counter(str(record.get("source_id")) for record in equation_records)
    figure_source_counts = Counter(str(record.get("source_id")) for record in figure_records)

    return {
        "page_slug": page_slug,
        "page_title": dossier.get("page_title"),
        "source_ids": source_ids,
        "families": sorted(families),
        "keywords": keyword_terms,
        "formula_candidate_count": len(equation_records),
        "figure_candidate_count": len(figure_records),
        "modern_visual_count": len(visual_records),
        "top_formula_candidates": sorted(equation_records, key=lambda item: -int(item.get("quality_score") or 0))[:8],
        "top_figure_candidates": figure_records[:8],
        "modern_visuals": visual_records[:6],
        "formula_source_counts": dict(formula_source_counts),
        "figure_source_counts": dict(figure_source_counts),
    }


def concept_bridge_markdown(bridge: dict[str, Any]) -> str:
    visual_cards = recreated_cards(bridge["modern_visuals"], compact=True)
    family_links = " - ".join(
        f"[{md_escape(FAMILY_LABELS.get(family, family))}]({family_link(family)})"
        for family in bridge.get("families") or []
    ) or "No formula family rule assigned yet."

    source_map_links = []
    source_ids = bridge.get("source_ids") or []
    for source_id in source_ids[:6]:
        source_map_links.append(
            f'<a href="{BASE_URL}/diagrams/source-visuals/{html_escape(source_id)}/">{html_escape(source_id)} visuals</a>'
        )
        source_map_links.append(
            f'<a href="{BASE_URL}/mathematics/source-formula-maps/{html_escape(source_id)}/">{html_escape(source_id)} formulas</a>'
        )
    source_link_text = " - ".join(source_map_links) if source_map_links else "No source maps are linked yet."

    formula_preview = formula_rows(bridge["top_formula_candidates"], 8)
    figure_preview = figure_rows(bridge["top_figure_candidates"], 8)
    return f"""{BEGIN_BRIDGE}

## Math And Visual Evidence Map

<div class="source-text-warning" data-layer="source">
  <strong>Generated bridge:</strong> this section crosslinks the concept page with the formula atlas, figure atlas, source visual maps, and source formula maps. It is a routing layer, not final interpretation.
</div>

<div class="codex-signal">
  <div><strong>{bridge['formula_candidate_count']}</strong><p>Formula candidates routed to this concept.</p></div>
  <div><strong>{bridge['figure_candidate_count']}</strong><p>Figure candidates routed to this concept.</p></div>
  <div><strong>{bridge['modern_visual_count']}</strong><p>Modern guide diagrams related to this concept.</p></div>
</div>

### Formula Families To Review

{family_links}

### Source Maps For This Concept

<div class="evidence-inline-links">
  {source_link_text}
</div>

### Related Modern Guide Diagrams

<div class="codex-visual-grid">
{visual_cards}
</div>

### Highest-Priority Formula Leads

<table class="codex-status-table evidence-candidate-table">
  <thead>
    <tr><th>Candidate</th><th>Family</th><th>OCR/PDF text</th><th>Routes</th></tr>
  </thead>
  <tbody>
{formula_preview}
  </tbody>
</table>

### Highest-Priority Figure Leads

<table class="codex-status-table evidence-candidate-table">
  <thead>
    <tr><th>Candidate</th><th>Caption lead</th><th>Section</th><th>Routes</th></tr>
  </thead>
  <tbody>
{figure_preview}
  </tbody>
</table>

{END_BRIDGE}
"""


def replace_bridge(existing: str, generated: str) -> str:
    pattern = re.compile(re.escape(BEGIN_BRIDGE) + r".*?" + re.escape(END_BRIDGE), flags=re.DOTALL)
    if pattern.search(existing):
        return pattern.sub(lambda _match: generated.strip(), existing).rstrip() + "\n"
    return existing.rstrip() + "\n\n" + generated.strip() + "\n"


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate source and concept crosslinked math/visual surfaces.")
    parser.add_argument("--root", default=".", help="Repository root")
    args = parser.parse_args()
    root = Path(args.root).resolve()

    equation_atlas = load_json(root / "processed" / "equation_atlas.json", {})
    figure_atlas = load_json(root / "processed" / "figure_candidate_atlas.json", {})
    visual_index = load_json(root / "processed" / "recreated_visual_index.json", {})
    dossier_index = load_json(root / "processed" / "concept_page_dossiers.json", {})
    extracted_index = load_json(root / "processed" / "extracted_visual_review_index.json", {})

    equations = list(equation_atlas.get("records") or [])
    figures = list(figure_atlas.get("candidate_records") or [])
    promoted = list(figure_atlas.get("promoted_original_figures") or [])
    visuals = list(visual_index.get("records") or [])
    equations_by_source = records_by_source(equations)
    figures_by_source = records_by_source(figures)
    promoted_by_source = records_by_source(promoted)
    visuals_by_source: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for visual in visuals:
        for source_id in visual.get("sources") or []:
            visuals_by_source[str(source_id)].append(visual)
    extracted_by_source: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for source_record in extracted_index.get("sources") or []:
        source_id = str(source_record.get("source_id") or "")
        if source_id:
            extracted_by_source[source_id].extend(source_record.get("records") or [])

    sources = sorted(build_source_meta(equation_atlas, figure_atlas).values(), key=lambda item: source_title(item))
    visual_root = root / "site" / "src" / "content" / "docs" / "diagrams" / "source-visuals"
    formula_root = root / "site" / "src" / "content" / "docs" / "mathematics" / "source-formula-maps"

    for source in sources:
        source_id = str(source.get("source_id"))
        write_text(
            visual_root / f"{source_id}.mdx",
            source_visual_page(
                source,
                figures_by_source.get(source_id, []),
                promoted_by_source.get(source_id, []),
                visuals_by_source.get(source_id, []),
                equations_by_source.get(source_id, []),
                extracted_by_source.get(source_id, []),
            ),
        )
        write_text(
            formula_root / f"{source_id}.mdx",
            source_formula_page(source, equations_by_source.get(source_id, [])),
        )

    write_text(
        visual_root / "index.mdx",
        source_visual_index(sources, equations_by_source, figures_by_source, promoted_by_source, visuals_by_source),
    )
    write_text(
        formula_root / "index.mdx",
        source_formula_index(sources, equations_by_source, figures_by_source, promoted_by_source, visuals_by_source),
    )

    bridges = []
    docs_concepts = root / "site" / "src" / "content" / "docs" / "concepts"
    for dossier in dossier_index.get("dossiers") or []:
        page_slug = str(dossier.get("page_slug") or "")
        path = docs_concepts / f"{page_slug}.mdx"
        if not page_slug or not path.exists():
            continue
        bridge = build_concept_bridge(dossier, equations_by_source, figures_by_source, visuals)
        existing = path.read_text(encoding="utf-8")
        write_text(path, replace_bridge(existing, concept_bridge_markdown(bridge)))
        bridges.append(bridge)

    output = {
        "generated_at": date.today().isoformat(),
        "quality_note": "Crosslinked research surfaces route source, math, visual, and concept evidence. Candidate records require scan and editorial verification.",
        "source_count": len(sources),
        "concept_bridge_count": len(bridges),
        "total_formula_candidates": len(equations),
        "total_figure_candidates": len(figures),
        "total_promoted_crops": len(promoted),
        "total_modern_visual_guides": len(visuals),
        "total_extracted_visual_candidates": sum(len(records) for records in extracted_by_source.values()),
        "sources": [
            {
                "source_id": source.get("source_id"),
                "title": source_title(source),
                "year": source.get("year"),
                "formula_candidates": len(equations_by_source.get(str(source.get("source_id")), [])),
                "figure_candidates": len(figures_by_source.get(str(source.get("source_id")), [])),
                "promoted_crops": len(promoted_by_source.get(str(source.get("source_id")), [])),
                "extracted_visual_candidates": len(extracted_by_source.get(str(source.get("source_id")), [])),
                "modern_visual_guides": len(visuals_by_source.get(str(source.get("source_id")), [])),
                "links": source_links(str(source.get("source_id"))),
            }
            for source in sources
        ],
        "concept_bridges": [
            {
                "page_slug": bridge.get("page_slug"),
                "page_title": bridge.get("page_title"),
                "formula_candidate_count": bridge.get("formula_candidate_count"),
                "figure_candidate_count": bridge.get("figure_candidate_count"),
                "modern_visual_count": bridge.get("modern_visual_count"),
                "families": bridge.get("families"),
                "keywords": bridge.get("keywords"),
            }
            for bridge in bridges
        ],
    }
    write_json(root / "processed" / "crosslinked_research_surfaces.json", output)
    write_json(root / "site" / "public" / "data" / "crosslinked_research_surfaces.json", output)
    print(
        f"Generated {len(sources)} source visual maps, {len(sources)} source formula maps, and {len(bridges)} concept bridges."
    )


if __name__ == "__main__":
    main()
