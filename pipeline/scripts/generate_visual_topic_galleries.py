def load_json(path: Path, default: Any) -> Any:
    if not path.exists():
        return default
    return json.loads(path.read_text(encoding="utf-8"))


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8", newline="\n")


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8", newline="\n")


def clean(value: Any, limit: int | None = None) -> str:
    text = "" if value is None else str(value)
    text = re.sub(r"\s+", " ", text).strip()
    if limit and len(text) > limit:
        text = text[: limit - 3].rstrip() + "..."
    return text


def esc(value: Any, limit: int | None = None) -> str:
    return html.escape(clean(value, limit), quote=True).replace("{", "&#123;").replace("}", "&#125;")


def slug_text(value: Any) -> str:
    return re.sub(r"[^a-z0-9]+", "-", clean(value).lower()).strip("-")


def haystack(record: dict[str, Any]) -> str:
    parts = [
        record.get("id"),
        record.get("title"),
        record.get("subtitle"),
        record.get("caption"),
        record.get("source_location"),
        record.get("filename"),
        record.get("source_title"),
        record.get("section_label"),
        record.get("original_form"),
        " ".join(str(item) for item in record.get("concepts") or []),
        " ".join(str(item) for item in record.get("sources") or []),
    ]
    return " ".join(clean(part).lower() for part in parts if part)


def matches(record: dict[str, Any], topic: dict[str, Any]) -> bool:
    text = haystack(record)
    terms = [*topic["concepts"], *topic["keywords"], *topic["sources"]]
    return any(term.lower() in text or slug_text(term) in text for term in terms)


def link_to_source_map(source_id: str, kind: str) -> str:
    if kind == "visual":
        return f"{BASE_URL}/diagrams/source-visuals/{source_id}/"
    if kind == "formula":
        return f"{BASE_URL}/mathematics/source-formula-maps/{source_id}/"
    if kind == "source":
        return f"{BASE_URL}/source-texts/{source_id}/"
    return f"{BASE_URL}/book-coverage/{source_id}/"


def visual_cards(records: list[dict[str, Any]]) -> str:
    if not records:
        return "<p>No modern guide diagrams are currently routed to this topic.</p>"
    cards = []
    for record in records:
        concepts = ", ".join(clean(item) for item in record.get("concepts") or [])
        cards.append(
            f"""<div class="codex-visual-card">
  <img src="{esc(record.get('public_url'))}" alt="{esc(record.get('title'))}" />
  <div>
    <strong>{esc(record.get('title'))}</strong>
    <p>{esc(record.get('subtitle'))}</p>
    <p><small>{esc(concepts)}</small></p>
    <p><a href="{esc(record.get('public_url'))}">Open SVG</a> - <a href="{BASE_URL}/diagrams/recreated-visual-index/">all recreated visuals</a></p>
  </div>
</div>"""
        )
    return "\n".join(cards)


def original_crop_records(root: Path, source_titles: dict[str, str]) -> list[dict[str, Any]]:
    records: list[dict[str, Any]] = []
    crop_root = root / "diagrams" / "original"
    if not crop_root.exists():
        return records
    for manifest in sorted(crop_root.glob("*/figures/*.png.json")):
        data = load_json(manifest, {})
        if not isinstance(data, dict):
            continue
        output_path = str(data.get("output_path") or "")
        if not output_path:
            continue
        public_path = output_path.replace("\\", "/")
        source_id = str(data.get("source_id") or manifest.parts[-3])
        public_url = f"{BASE_URL}/{public_path}"
        title = manifest.name.replace(".png.json", "").replace("-", " ").title()
        records.append(
            {
                "id": data.get("id") or manifest.stem,
                "title": title,
                "filename": manifest.name.replace(".json", ""),
                "source_id": source_id,
                "source_title": source_titles.get(source_id, source_id.replace("-", " ").title()),
                "source_location": data.get("source_location") or "",
                "caption": data.get("quality_note") or "Promoted original scan crop.",
                "public_url": public_url,
                "status": data.get("status") or "promoted original scan crop",
                "links": {
                    "source_text_index": f"{BASE_URL}/source-texts/{source_id}/",
                    "visual_map": f"{BASE_URL}/diagrams/source-visuals/{source_id}/",
                    "diagram_archive": f"{BASE_URL}/diagrams/",
                },
            }
        )
    return records


