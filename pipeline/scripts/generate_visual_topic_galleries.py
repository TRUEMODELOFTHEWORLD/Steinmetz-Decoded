def source_links(topic: dict[str, Any]) -> str:
    links = []
    for source_id in topic["sources"]:
        links.append(
            f"""<a href="{link_to_source_map(source_id, 'visual')}">{esc(source_id.replace('-', ' ').title())}
<span>Visual map</span></a>"""
        )
        links.append(
            f"""<a href="{link_to_source_map(source_id, 'formula')}">{esc(source_id.replace('-', ' ').title())}
<span>Formula map</span></a>"""
        )
    return "\n".join(links)


