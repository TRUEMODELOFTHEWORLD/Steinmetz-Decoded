def index_page(topic_summaries: list[dict[str, Any]]) -> str:
    cards = []
    for item in topic_summaries:
        cards.append(
            f"""<a href="{BASE_URL}/diagrams/visual-topic-galleries/{item['id']}/">
  <strong>{esc(item['title'])}</strong>
  <span>{esc(item['description'], 150)}</span>
  <small>{item['original_crop_count']} original crops - {item['visual_count']} guide visuals - {item['figure_count']} figure leads - {item['formula_count']} formula leads</small>
</a>"""
        )
    return f"""---
title: Visual Topic Galleries
description: Topic-level visual routes through Steinmetz diagrams, figure candidates, and formula maps.
---

{GENERATED_NOTE}

The visual topic galleries are the bridge between casual visual discovery and the archive's rigorous evidence system. Each route collects modern guide diagrams, figure candidates, source maps, workbench links, and formula leads around a major Steinmetz research theme.

<div class="source-text-warning" data-layer="source">
  <strong>Use rule:</strong> browse here to find a path, then cite from the source text, scans, and verification queues. Modern guide diagrams are explanatory reconstructions.
</div>

<div class="reading-route-index visual-topic-index">
{chr(10).join(cards)}
</div>
"""


def main() -> int:
    root = Path(__file__).resolve().parents[2]
    visual_index = load_json(root / "processed" / "recreated_visual_index.json", {})
    figure_atlas = load_json(root / "processed" / "figure_candidate_atlas.json", {})
    equation_atlas = load_json(root / "processed" / "equation_atlas.json", {})
    source_catalog = load_json(root / "sources" / "source_catalog.json", [])
    source_titles = {
        str(source.get("source_id")): str(source.get("title") or source.get("source_id"))
        for source in source_catalog
        if isinstance(source, dict) and source.get("source_id")
    }

    visual_records = list(visual_index.get("records") or [])
    figure_records = list(figure_atlas.get("candidate_records") or [])
    formula_records = list(equation_atlas.get("records") or [])
    crop_records = original_crop_records(root, source_titles)

    output_root = root / "site" / "src" / "content" / "docs" / "diagrams" / "visual-topic-galleries"
    summaries = []
    payload_topics = []
    for topic in TOPICS:
        topic_visuals = [record for record in visual_records if matches(record, topic)]
        topic_crops = [record for record in crop_records if matches(record, topic)]
        topic_figures = [record for record in figure_records if matches(record, topic)]
        topic_formulas = sorted(
            [record for record in formula_records if matches(record, topic)],
            key=lambda item: -int(item.get("quality_score") or 0),
        )
        write_text(output_root / f"{topic['id']}.mdx", topic_page(topic, topic_visuals, topic_crops, topic_figures, topic_formulas))
        summaries.append(
            {
                "id": topic["id"],
                "title": topic["title"],
                "description": topic["description"],
                "original_crop_count": len(topic_crops),
                "visual_count": len(topic_visuals),
                "figure_count": len(topic_figures),
                "formula_count": len(topic_formulas),
            }
        )
        payload_topics.append(
            {
                **summaries[-1],
                "sources": topic["sources"],
                "concepts": topic["concepts"],
                "keywords": topic["keywords"],
                "links": {
                    "page": f"{BASE_URL}/diagrams/visual-topic-galleries/{topic['id']}/",
                    "diagram_archive": f"{BASE_URL}/diagrams/",
                    "figure_atlas": f"{BASE_URL}/diagrams/figure-candidate-atlas/",
                    "equation_atlas": f"{BASE_URL}/mathematics/equation-atlas/",
                },
            }
        )

    write_text(output_root / "index.mdx", index_page(summaries))
    payload = {
        "generated_at": date.today().isoformat(),
        "quality_note": "Visual topic galleries are discovery routes over source, figure, visual, and formula candidate layers.",
        "topic_count": len(payload_topics),
        "topics": payload_topics,
    }
    write_json(root / "processed" / "visual_topic_galleries.json", payload)
    write_json(root / "site" / "public" / "data" / "visual_topic_galleries.json", payload)
    print(f"Generated {len(payload_topics)} visual topic galleries.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
