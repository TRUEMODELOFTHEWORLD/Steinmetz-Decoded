def flatten_sections(coverage: dict[str, Any]) -> list[dict[str, Any]]:
    sections: list[dict[str, Any]] = []
    for source in coverage.get("sources") or []:
        source_id = source.get("source_id") or source.get("id")
        source_links = source.get("links") or {}
        for section in source.get("sections") or []:
            links = section.get("links") or {}
            themes = compact_list(section.get("top_themes") or [], 6)
            concepts = compact_list(section.get("top_concepts") or [], 6)
            search_text = " ".join(
                [
                    str(source_id or ""),
                    str(source.get("title") or ""),
                    str(section.get("label") or ""),
                    " ".join(themes),
                    " ".join(concepts),
                ]
            )
            sections.append(
                {
                    "source_id": source_id,
                    "source_title": source.get("title"),
                    "source_year": source.get("year"),
                    "section_id": section.get("id"),
                    "section_slug": section.get("slug"),
                    "label": section.get("label"),
                    "status": section.get("status"),
                    "word_count": int(section.get("word_count") or 0),
                    "equation_count": int(section.get("equation_count") or 0),
                    "figure_count": int(section.get("figure_count") or 0),
                    "quote_count": int(section.get("quote_count") or 0),
                    "top_themes": themes,
                    "top_concepts": concepts,
                    "links": {
                        "source_text": site_url(links.get("source_text")),
                        "workbench": site_url(links.get("workbench")),
                        "book_coverage": site_url(source_links.get("coverage")),
                        "source_overview": site_url(source_links.get("curated_source")),
                        "archive": site_url(source_links.get("archive")),
                    },
                    "search_text": slug_text(search_text),
                }
            )
    return sections


def keyword_score(text: str, keywords: list[str]) -> int:
    score = 0
    for keyword in keywords:
        needle = slug_text(keyword).strip()
        if not needle:
            continue
        if needle in text:
            score += 36 if " " in needle else 18
    return score


def source_score(source_id: str, preferred_sources: list[str]) -> int:
    try:
        position = preferred_sources.index(source_id)
    except ValueError:
        return -60 if preferred_sources else 0
    return max(40, 120 - position * 8)


def score_section(section: dict[str, Any], route: dict[str, Any]) -> int:
    weights = route.get("feature_weights") or {}
    score = keyword_score(section["search_text"], route.get("keywords") or [])
    score += source_score(str(section.get("source_id") or ""), route.get("preferred_sources") or [])
    score += int(section.get("figure_count") or 0) * int(weights.get("figures") or 0)
    score += int(section.get("equation_count") or 0) * int(weights.get("equations") or 0)
    score += int(section.get("quote_count") or 0) * int(weights.get("quotes") or 0)
    score += min(24, int(section.get("word_count") or 0) // 1200) * int(weights.get("words") or 0)
    return score


def select_sections(sections: list[dict[str, Any]], route: dict[str, Any]) -> list[dict[str, Any]]:
    scored: list[dict[str, Any]] = []
    preferred_sources = route.get("preferred_sources") or []
    strict_preferred = bool(route.get("strict_preferred"))
    for section in sections:
        if strict_preferred and preferred_sources and section.get("source_id") not in preferred_sources:
            continue
        score = score_section(section, route)
        if score <= 0:
            continue
        enriched = {key: value for key, value in section.items() if key != "search_text"}
        enriched["route_score"] = score
        scored.append(enriched)
    scored.sort(
        key=lambda item: (
            -int(item.get("route_score") or 0),
            str(item.get("source_title") or ""),
            str(item.get("label") or ""),
        )
    )
    limit = int(route.get("section_limit") or 10)
    max_per_source = int(route.get("max_per_source") or 4)
    selected: list[dict[str, Any]] = []
    source_counts: dict[str, int] = {}
    for item in scored:
        source_id = str(item.get("source_id") or "")
        if source_counts.get(source_id, 0) >= max_per_source:
            continue
        selected.append(item)
        source_counts[source_id] = source_counts.get(source_id, 0) + 1
        if len(selected) >= limit:
            break
    return selected


def build_routes(root: Path) -> dict[str, Any]:
    coverage = load_json(root / "processed" / "book_coverage_atlas.json", {})
    sections = flatten_sections(coverage)
    routes: list[dict[str, Any]] = []
    selected_ids: set[str] = set()
    for route in ROUTES:
        route_sections = select_sections(sections, route)
        for section in route_sections:
            selected_ids.add(f"{section.get('source_id')}::{section.get('section_id')}")
        source_count = len({section.get("source_id") for section in route_sections})
        routes.append(
            {
                "id": route["id"],
                "title": route["title"],
                "audience": route["audience"],
                "why_it_exists": route["why_it_exists"],
                "reading_rule": route["reading_rule"],
                "start_links": [
                    {"label": label, "url": url, "note": note}
                    for label, url, note in route.get("start_links", [])
                ],
                "keywords": route.get("keywords") or [],
                "source_count": source_count,
                "section_count": len(route_sections),
                "equation_candidate_count": sum(int(section.get("equation_count") or 0) for section in route_sections),
                "figure_candidate_count": sum(int(section.get("figure_count") or 0) for section in route_sections),
                "quote_candidate_count": sum(int(section.get("quote_count") or 0) for section in route_sections),
                "sections": route_sections,
            }
        )
    return {
        "generated_at": date.today().isoformat(),
        "quality_note": (
            "Guided routes are generated from processed source metadata, theme hits, concept hits, "
            "and candidate equation/figure/quote counts. They are navigation aids, not final scholarly judgments."
        ),
        "route_count": len(routes),
        "source_count": int(coverage.get("source_count") or len(coverage.get("sources") or [])),
        "processed_section_count": len(sections),
        "selected_unique_section_count": len(selected_ids),
        "routes": routes,
    }


