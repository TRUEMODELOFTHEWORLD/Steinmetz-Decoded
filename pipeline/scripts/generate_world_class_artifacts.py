def load_json(path: Path, default: Any) -> Any:
    if not path.exists():
        return default
    return json.loads(path.read_text(encoding="utf-8"))


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def md_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace("|", "\\|")
        .replace("\n", " ")
    )


def html_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def slug_label(path: str) -> str:
    stem = Path(path).stem
    return stem.replace("-", " ").replace("_", " ").title()


def source_lookup(root: Path) -> dict[str, dict[str, Any]]:
    catalog = load_json(root / "sources" / "source_catalog.json", [])
    if not isinstance(catalog, list):
        return {}
    return {str(source.get("source_id")): source for source in catalog if source.get("source_id")}


def symbol_present(symbol: str, text: str) -> bool:
    escaped = re.escape(symbol)
    compact = re.sub(r"\s+", "", text)
    if symbol == "j" and re.search(r"(?<![A-Za-z0-9_'])j(?=x|[^A-Za-z0-9_']|$)", text):
        return True
    if symbol == "x" and re.search(r"(?<![A-Za-z0-9_'])(?:x(?:_[A-Za-z0-9]+)?|jx)(?![A-Za-z0-9_'])", text):
        return True
    if symbol == "V" and "VI" in compact:
        return True
    if symbol == "I" and "VI" in compact:
        return True
    if symbol == "e" and "ei" in compact:
        return True
    if symbol == "i" and "ei" in compact:
        return True
    if symbol.endswith("'"):
        return symbol in text
    return bool(re.search(rf"(?<![A-Za-z0-9_']){escaped}(?![A-Za-z0-9_'])", text))


def build_notation_ledger(root: Path) -> dict[str, Any]:
    equations = load_json(root / "processed" / "canonical_equations.json", {}).get("records", [])
    symbol_index: dict[str, dict[str, Any]] = {}
    records = []

    for equation in equations:
        forms = f"{equation.get('original_form', '')} {equation.get('modern_form', '')}"
        symbol_map = []
        for symbol, note in SYMBOL_NOTES.items():
            if symbol_present(symbol, forms):
                symbol_map.append(
                    {
                        "symbol": symbol,
                        "meaning_note": note,
                        "review_state": "needs source-context verification",
                    }
                )
                symbol_index.setdefault(
                    symbol,
                    {
                        "symbol": symbol,
                        "meaning_note": note,
                        "equation_ids": [],
                        "source_ids": [],
                    },
                )
                symbol_index[symbol]["equation_ids"].append(equation.get("id"))
                if equation.get("source_id") not in symbol_index[symbol]["source_ids"]:
                    symbol_index[symbol]["source_ids"].append(equation.get("source_id"))

        records.append(
            {
                "equation_id": equation.get("id"),
                "title": equation.get("title"),
                "source_id": equation.get("source_id"),
                "source_title": equation.get("source_title"),
                "original_form": equation.get("original_form"),
                "modern_form": equation.get("modern_form"),
                "status": equation.get("status"),
                "source_ref": equation.get("source_ref", {}),
                "site_path": equation.get("site_path"),
                "symbols": symbol_map,
                "review_actions": [
                    "Check original typography and subscripts against scan.",
                    "Confirm whether quantities are RMS, maximum, instantaneous, or symbolic.",
                    "Record unit conventions and edition differences if found.",
                ],
            }
        )

    return {
        "generated_at": date.today().isoformat(),
        "quality_note": "Notation ledger for review. It maps source and modern symbols but does not certify typography until scan verification is complete.",
        "record_count": len(records),
        "symbol_count": len(symbol_index),
        "symbol_index": sorted(symbol_index.values(), key=lambda item: item["symbol"].lower()),
        "records": records,
    }


def public_diagram_url(path: str) -> str:
    normalized = path.replace("\\", "/")
    if normalized.startswith("diagrams/"):
        return f"{BASE_URL}/{normalized}"
    if normalized.startswith("site/public/"):
        return f"{BASE_URL}/{normalized.removeprefix('site/public/')}"
    return f"{BASE_URL}/{normalized}"


def infer_redraw_source(path: Path) -> str:
    name = path.as_posix().lower()
    if "alternating" in name or "ac-" in name or "phasor" in name or "impedance" in name:
        return "theory-calculation-alternating-current-phenomena"
    if "transient" in name:
        return "theory-calculation-transient-electric-phenomena-oscillations"
    if "radiation" in name or "illumination" in name or "spectrum" in name:
        return "radiation-light-and-illumination"
    return "cross-source-or-conceptual"


def build_diagram_provenance_ledger(root: Path) -> dict[str, Any]:
    sources = source_lookup(root)
    original_crops = []
    for manifest_path in sorted((root / "diagrams" / "original").rglob("*.json")):
        manifest = load_json(manifest_path, {})
        source_id = manifest.get("source_id")
        output_path = str(manifest.get("output_path") or manifest_path.with_suffix("").relative_to(root))
        original_crops.append(
            {
                "id": manifest.get("id") or manifest_path.stem,
                "asset_type": manifest.get("asset_type", "original-scan-crop"),
                "source_id": source_id,
                "source_title": sources.get(str(source_id), {}).get("title"),
                "status": manifest.get("status"),
                "source_location": manifest.get("source_location"),
                "source_image": manifest.get("source_image"),
                "crop_box_pixels": manifest.get("crop_box_pixels"),
                "output_path": output_path,
                "public_url": public_diagram_url(output_path),
                "width": manifest.get("width"),
                "height": manifest.get("height"),
                "sha256": manifest.get("sha256"),
                "quality_note": manifest.get("quality_note"),
                "manifest_path": manifest_path.relative_to(root).as_posix(),
            }
        )

    public_redraws_by_name = {
        path.name: path.relative_to(root).as_posix()
        for path in (root / "site" / "public" / "diagrams").rglob("*.svg")
    }
    redraws = []
    for svg_path in sorted((root / "diagrams" / "recreated").rglob("*.svg")):
        public_path = public_redraws_by_name.get(svg_path.name)
        rel_path = svg_path.relative_to(root).as_posix()
        redraws.append(
            {
                "id": svg_path.stem,
                "asset_type": "modern-redraw-or-reading-aid",
                "source_id": infer_redraw_source(svg_path.relative_to(root)),
                "status": "reconstruction; must remain visually distinct from original scan crop",
                "source_path": rel_path,
                "public_url": public_diagram_url(public_path or rel_path),
                "verification": "needs source-link and annotation review before canonical diagram status",
                "review_actions": [
                    "Confirm which original figure or concept the redraw explains.",
                    "Ensure annotations are not mistaken for Steinmetz's original drawing.",
                    "Link redraw to the original crop when available.",
                ],
            }
        )

    status_counts = Counter(item["status"] for item in original_crops)
    return {
        "generated_at": date.today().isoformat(),
        "quality_note": "Diagram provenance ledger. Original crops are scan-derived assets; redraws are interpretive/reconstructive reading aids unless separately reviewed.",
        "original_crop_count": len(original_crops),
        "redraw_count": len(redraws),
        "original_crop_status_counts": dict(status_counts),
        "original_crops": original_crops,
        "redraws": redraws,
    }


def sample_schema(data: Any) -> dict[str, Any]:
    if isinstance(data, list):
        sample = data[0] if data else {}
        return {
            "container": "array",
            "top_level_fields": sorted(sample.keys()) if isinstance(sample, dict) else [],
            "record_count": len(data),
        }
    if isinstance(data, dict):
        sample = None
        for key in ("records", "source_audits", "exports", "packets", "original_crops"):
            value = data.get(key)
            if isinstance(value, list) and value:
                sample = value[0]
                break
        fields = sorted(sample.keys()) if isinstance(sample, dict) else sorted(data.keys())
        return {
            "container": "object",
            "top_level_fields": sorted(data.keys()),
            "sample_record_fields": fields,
            "record_count": len(data.get("records", [])) if isinstance(data.get("records"), list) else None,
        }
    return {"container": type(data).__name__, "top_level_fields": [], "record_count": None}


def review_fields(fields: list[str]) -> list[str]:
    markers = ("status", "review", "verification", "confidence", "quality", "source_ref")
    return [field for field in fields if any(marker in field.lower() for marker in markers)]


def build_schema_reference(root: Path) -> dict[str, Any]:
    files = []
    for file_id, rel_path, description in SCHEMA_FILES:
        path = root / rel_path
        exists = path.exists()
        schema = sample_schema(load_json(path, {})) if exists and path.suffix == ".json" else {}
        fields = schema.get("sample_record_fields") or schema.get("top_level_fields") or []
        files.append(
            {
                "id": file_id,
                "description": description,
                "path": rel_path,
                "exists": exists,
                "public_url": f"{BASE_URL}/data/{file_id}.json" if rel_path.startswith("processed/") or rel_path.startswith("sources/") else None,
                "schema_summary": schema,
                "review_state_fields": review_fields(fields),
                "usage_rule": "Use review-state fields before citing any record as fact.",
            }
        )
    return {
        "generated_at": date.today().isoformat(),
        "quality_note": "Schema reference for public and processed data. It is descriptive, not a formal JSON Schema validator yet.",
        "files": files,
    }


