def build_notation_page(ledger: dict[str, Any]) -> str:
    rows = []
    for record in ledger["records"]:
        symbols = ", ".join(f"`{item['symbol']}`" for item in record["symbols"]) or "needs mapping"
        rows.append(
            f"| `{md_escape(record['equation_id'])}` | {md_escape(record['title'])} | {symbols} | {md_escape(record['status'])} | {md_escape(record.get('source_ref', {}).get('verification'))} |"
        )
    symbol_rows = []
    for item in ledger["symbol_index"]:
        equation_ids = ", ".join(f"`{equation_id}`" for equation_id in item["equation_ids"])
        symbol_rows.append(f"| `{md_escape(item['symbol'])}` | {md_escape(item['meaning_note'])} | {equation_ids} |")

    return f"""---
title: Notation Ledger
description: Source and modern notation review ledger for Steinmetz equations.
---

{GENERATED_NOTE}

## Notation Ledger

The notation ledger prevents a quiet failure mode: translating Steinmetz into modern symbols so quickly that the older notation, assumptions, and physical language disappear. Each row remains a review target until the original typography, source context, and units are checked.

<div class="source-text-warning" data-layer="source">
  <strong>Boundary:</strong> this ledger maps notation for review. It does not certify an equation as scan-verified.
</div>

## Equation Records

| Equation | Title | Symbols Tracked | Status | Verification |
| --- | --- | --- | --- | --- |
{chr(10).join(rows)}

## Symbol Index

| Symbol | Review Meaning | Equation Records |
| --- | --- | --- |
{chr(10).join(symbol_rows)}

## Reviewer Rule

Preserve Steinmetz's original notation first. Add modern notation second. If a symbol changes meaning across sources or editions, record the difference instead of smoothing it away.
"""


