def load_json(path: Path, default: Any) -> Any:
    if not path.exists():
        return default
    return json.loads(path.read_text(encoding="utf-8"))


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def clean_text(value: Any, limit: int | None = None) -> str:
    text = "" if value is None else str(value)
    replacements = {
        "\u00a0": " ",
        "\u2013": "-",
        "\u2014": "-",
        "\u2018": "'",
        "\u2019": "'",
        "\u201c": '"',
        "\u201d": '"',
        "\ufeff": "",
    }
    for old, new in replacements.items():
        text = text.replace(old, new)
    text = re.sub(r"\s+", " ", text).strip()
    if limit and len(text) > limit:
        text = text[: limit - 3].rstrip() + "..."
    return text


def html_escape(value: Any) -> str:
    text = html.escape(clean_text(value), quote=True)
    return (
        text.replace("\\", "&#92;")
        .replace("`", "&#96;")
        .replace("$", "&#36;")
        .replace("{", "&#123;")
        .replace("}", "&#125;")
        .replace("*", "&#42;")
        .replace("~", "&#126;")
        .replace("_", "&#95;")
        .replace("[", "&#91;")
        .replace("]", "&#93;")
    )


def html_math_escape(value: Any) -> str:
    return (
        html.escape(clean_text(value), quote=True)
        .replace("`", "&#96;")
        .replace("$", "&#36;")
        .replace("{", "&#123;")
        .replace("}", "&#125;")
        .replace("*", "&#42;")
        .replace("~", "&#126;")
        .replace("_", "&#95;")
        .replace("[", "&#91;")
        .replace("]", "&#93;")
    )


def latex_candidate(value: Any) -> str:
    text = clean_text(value, 220)
    text = html.unescape(text)
    replacements = {
        "—": "-",
        "–": "-",
        "π": r"\pi",
        "λ": r"\lambda",
        "ω": r"\omega",
        "θ": r"\theta",
        "φ": r"\phi",
    }
    for old, new in replacements.items():
        text = text.replace(old, new)
    text = re.sub(r"\bpi\b", r"\\pi", text, flags=re.I)
    text = re.sub(r"\blambda\b", r"\\lambda", text, flags=re.I)
    text = re.sub(r"\bomega\b", r"\\omega", text, flags=re.I)
    text = re.sub(r"\btheta\b", r"\\theta", text, flags=re.I)
    text = re.sub(r"\bphi\b", r"\\phi", text, flags=re.I)
    text = re.sub(r"\b(sin|cos|tan|log)\b", lambda match: "\\" + match.group(1).lower(), text, flags=re.I)
    text = re.sub(r"\bdeg\.?", r"^\\circ", text, flags=re.I)
    text = re.sub(r"\be\.?m\.?f\.?", r"\\mathrm{e.m.f.}", text, flags=re.I)
    text = text.replace("&", r"\&").replace("%", r"\%").replace("#", r"\#").replace("$", r"\$")
    return text


def md_escape(value: Any) -> str:
    return (
        clean_text(value)
        .replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace("|", "\\|")
        .replace("{", "&#123;")
        .replace("}", "&#125;")
    )


def site_url(path: str | None) -> str:
    if not path:
        return ""
    if path.startswith(BASE_URL):
        return path
    if path.startswith("/"):
        return f"{BASE_URL}{path}"
    return f"{BASE_URL}/{path}"


def find_section(workbench_records: list[dict[str, Any]], source_id: str, line: int | None) -> dict[str, Any] | None:
    if line is None:
        return None
    for record in workbench_records:
        if record.get("source_id") != source_id:
            continue
        start = record.get("line_start")
        end = record.get("line_end")
        if isinstance(start, int) and isinstance(end, int) and start <= line <= end:
            return record
    return None


def formula_score(text: str) -> tuple[int, list[str]]:
    raw = clean_text(text)
    lower = raw.lower()
    signals: list[str] = []
    score = 0

    if "=" in raw:
        score += 34
        signals.append("contains equals sign")
    if re.search(r"\b[a-zA-Z][a-zA-Z0-9_']{0,3}\s*=", raw):
        score += 12
        signals.append("left-hand symbol")
    if re.search(r"[+\-*/^()]", raw):
        score += 10
        signals.append("operator characters")
    if re.search(r"\b(sin|cos|tan|log|sqrt|exp|differential|integral)\b", lower):
        score += 12
        signals.append("function/calculus language")
    if re.search(r"\b(pi|omega|lambda|theta|phi|j|e\.m\.f|e.m.f|rms)\b", lower):
        score += 12
        signals.append("technical symbol language")
    if re.search(r"\d", raw) and re.search(r"[+\-*/=^]", raw):
        score += 10
        signals.append("numeric relation")
    if re.search(r"\b(where|let|if|then|therefore|hence)\b", lower) and "=" in raw:
        score += 8
        signals.append("equation explanation context")
    if re.search(r"\b(fig|figure)\.?\s*\d+", lower):
        score -= 18
        signals.append("figure reference contamination")
    if re.match(r"^\d+\.\s+[A-Z][^.]+\.?\s+\d{1,4}\.?$", raw) and "=" not in raw:
        score -= 32
        signals.append("table-of-contents-like")
    words = re.findall(r"[A-Za-z]{3,}", raw)
    tokens = max(1, len(re.findall(r"\S+", raw)))
    if len(words) / tokens > 0.72 and "=" not in raw:
        score -= 18
        signals.append("mostly prose")
    if len(raw) > 190:
        score -= 12
        signals.append("long OCR line")
    if len(raw) < 4:
        score -= 20
        signals.append("too short")

    return max(0, score), signals


def quality_band(score: int) -> str:
    if score >= 58:
        return "strong-formula-candidate"
    if score >= 36:
        return "reviewable-relation-candidate"
    if score >= 22:
        return "weak-math-or-definition-candidate"
    return "low-confidence-noise-candidate"


def word_hits(lower: str, patterns: list[str]) -> int:
    hits = 0
    for pattern in patterns:
        escaped = re.escape(pattern.lower()).replace("\\ ", r"\s+")
        hits += len(re.findall(rf"\b{escaped}\b", lower))
    return hits


def classify_family(text: str, section_label: str = "", source_id: str = "", source_title: str = "") -> str:
    lower = f" {clean_text(text).lower()} {clean_text(section_label).lower()} {clean_text(source_title).lower()} "

    if source_id == "engineering-mathematics":
        return "engineering-math"
    if "commonwealth-edison" in source_id:
        return "apparatus-systems"
    if "transient" in source_id or "discharges-waves-impulses" in source_id:
        return "transients-oscillation"
    if re.search(r"(^|[^a-z])j([^a-z]|$)", lower) or "complex" in lower or "imaginary" in lower or "quadrature" in lower:
        return "symbolic-ac"
    if re.search(r"\b(cos|sin|tan)\b", lower) and ("phase" in lower or "=" in text):
        return "symbolic-ac"

    best_family = "general-equation-candidates"
    best_score = 0
    for family_id, family in FAMILIES.items():
        score = word_hits(lower, family["patterns"])
        if score > best_score:
            best_score = score
            best_family = family_id
    return best_family


def enrich_record(record: dict[str, Any], workbench_records: list[dict[str, Any]]) -> dict[str, Any]:
    source_ref = record.get("source_ref") or {}
    source_id = str(record.get("source_id") or source_ref.get("source_id") or "")
    line = source_ref.get("line_start")
    line_number = line if isinstance(line, int) else None
    section = find_section(workbench_records, source_id, line_number)
    section_label = section.get("label") if section else ""
    score, signals = formula_score(str(record.get("original_form") or ""))
    family = classify_family(
        str(record.get("original_form") or ""),
        str(section_label),
        source_id,
        str(record.get("source_title") or ""),
    )

    links = {
        "source_overview": f"{BASE_URL}/sources/{source_id}/",
        "source_text_index": f"{BASE_URL}/source-texts/{source_id}/",
        "workbench_index": f"{BASE_URL}/chapter-workbench/{source_id}/",
        "equation_verification_queue": f"{BASE_URL}/roadmap/equation-verification-queue/",
    }
    if section:
        links.update(
            {
                "source_text": site_url(section.get("links", {}).get("source_text")),
                "workbench": f"{BASE_URL}/chapter-workbench/{source_id}/{section.get('slug')}/",
                "book_coverage": f"{BASE_URL}/book-coverage/{source_id}/",
            }
        )

    return {
        "id": record.get("id"),
        "source_id": source_id,
        "source_title": record.get("source_title"),
        "original_form": clean_text(record.get("original_form"), 360),
        "modern_form": clean_text(record.get("modern_form"), 260),
        "status": record.get("status"),
        "source_ref": source_ref,
        "line_start": line_number,
        "line_end": source_ref.get("line_end"),
        "verification": source_ref.get("verification"),
        "section_id": section.get("id") if section else None,
        "section_label": section_label,
        "section_slug": section.get("slug") if section else None,
        "quality_score": score,
        "quality_band": quality_band(score),
        "signals": signals,
        "family": family,
        "links": links,
    }


def build_atlas(root: Path) -> dict[str, Any]:
    equation_index = load_json(root / "processed" / "equation_index.json", {})
    workbench = load_json(root / "processed" / "chapter_workbench.json", {})
    workbench_records = list(workbench.get("records") or [])
    source_catalog = load_json(root / "sources" / "source_catalog.json", [])
    source_meta = {
        str(source.get("source_id")): {
            "source_id": source.get("source_id"),
            "title": source.get("title"),
            "year": source.get("year"),
            "coverage": f"{BASE_URL}/book-coverage/{source.get('source_id')}/",
            "source_text": f"{BASE_URL}/source-texts/{source.get('source_id')}/",
            "workbench": f"{BASE_URL}/chapter-workbench/{source.get('source_id')}/",
        }
        for source in source_catalog
        if isinstance(source, dict)
    }

    records = [enrich_record(record, workbench_records) for record in equation_index.get("records", [])]
    records.sort(key=lambda item: (-int(item.get("quality_score") or 0), str(item.get("source_title")), int(item.get("line_start") or 999999999)))

    by_family: dict[str, list[dict[str, Any]]] = defaultdict(list)
    by_source: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for record in records:
        by_family[str(record["family"])].append(record)
        by_source[str(record["source_id"])].append(record)

    source_summaries = []
    for source_id, source_records in sorted(by_source.items(), key=lambda item: len(item[1]), reverse=True):
        strong = [item for item in source_records if int(item.get("quality_score") or 0) >= 36]
        source_summaries.append(
            {
                **source_meta.get(source_id, {"source_id": source_id, "title": source_id}),
                "candidate_count": len(source_records),
                "reviewable_relation_count": len(strong),
                "section_linked_count": sum(1 for item in source_records if item.get("section_id")),
                "top_families": Counter(str(item.get("family")) for item in strong).most_common(5),
            }
        )

    return {
        "generated_at": date.today().isoformat(),
        "quality_note": "Equation atlas records are OCR/PDF-text candidates. Promote only after scan verification, mathematical transcription, and notation review.",
        "total_candidate_records": len(records),
        "reviewable_relation_count": sum(1 for item in records if int(item.get("quality_score") or 0) >= 36),
        "strong_formula_count": sum(1 for item in records if int(item.get("quality_score") or 0) >= 58),
        "source_count": len(by_source),
        "family_counts": {family_id: len(items) for family_id, items in sorted(by_family.items())},
        "source_summaries": source_summaries,
        "records": records,
    }


def card(record: dict[str, Any], rank: int | None = None) -> str:
    rank_html = f"<span>#{rank}</span>" if rank is not None else ""
    line = record.get("line_start")
    section = record.get("section_label") or "source index"
    source_link = record.get("links", {}).get("source_text") or record.get("links", {}).get("source_text_index")
    workbench_link = record.get("links", {}).get("workbench") or record.get("links", {}).get("workbench_index")
    original = record.get("original_form")
    latex = latex_candidate(original)
    return f"""<article class="equation-candidate-card" data-layer="math-candidate">
  <header>
    {rank_html}
    <strong>{html_escape(record.get("source_title"))}</strong>
    <small>{html_escape(section)}{html_escape(" - line " + str(line) if line else "")}</small>
  </header>
  <span class="equation-render">\\({html_math_escape(latex)}\\)</span>
  <details class="equation-source-line"><summary>OCR source line</summary><code>{html_escape(original)}</code></details>
  <footer>
    <small>{html_escape(record.get("quality_band"))} - score {html_escape(record.get("quality_score"))} - {html_escape(FAMILIES.get(str(record.get("family")), {}).get("label", "General candidates"))}</small>
    <nav>
      <a href="{html_escape(source_link)}">source text</a>
      <a href="{html_escape(workbench_link)}">workbench</a>
      <a href="{BASE_URL}/roadmap/equation-verification-queue/">verification queue</a>
    </nav>
  </footer>
</article>"""


def source_rows(atlas: dict[str, Any]) -> str:
    rows = []
    for source in atlas.get("source_summaries", []):
        rows.append(
            "<tr>"
            f"<td><a href=\"{html_escape(source.get('coverage'))}\">{html_escape(source.get('title'))}</a></td>"
            f"<td>{html_escape(source.get('year') or '')}</td>"
            f"<td>{html_escape(source.get('candidate_count'))}</td>"
            f"<td>{html_escape(source.get('reviewable_relation_count'))}</td>"
            f"<td>{html_escape(source.get('section_linked_count'))}</td>"
            f"<td><a href=\"{html_escape(source.get('source_text'))}\">source</a> - <a href=\"{html_escape(source.get('workbench'))}\">workbench</a></td>"
            "</tr>"
        )
    return "\n".join(rows)


def family_cards(atlas: dict[str, Any]) -> str:
    family_counts = atlas.get("family_counts", {})
    parts = []
    for family_id, family in FAMILIES.items():
        parts.append(
            f'<a href="{BASE_URL}/mathematics/equation-atlas/{family_id}/">{html_escape(family["label"])}'
            f'<span>{html_escape(family_counts.get(family_id, 0))} candidates. {html_escape(family["why"])}</span></a>'
        )
    parts.append(
        f'<a href="{BASE_URL}/mathematics/equation-atlas/general-equation-candidates/">General Equation Candidates'
        f'<span>{html_escape(family_counts.get("general-equation-candidates", 0))} candidates awaiting classification.</span></a>'
    )
    return "\n".join(parts)


