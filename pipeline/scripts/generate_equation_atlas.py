def write_pages(root: Path, atlas: dict[str, Any]) -> None:
    docs = root / "site" / "src" / "content" / "docs" / "mathematics" / "equation-atlas"
    docs.mkdir(parents=True, exist_ok=True)
    write_text(docs / "index.mdx", build_index_page(atlas))
    family_ids = set(FAMILIES) | {"general-equation-candidates"}
    for family_id in sorted(family_ids):
        write_text(docs / family_id / "index.mdx", build_family_page(family_id, atlas))


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--root", default=".", help="Repository root")
    args = parser.parse_args()
    root = Path(args.root).resolve()
    atlas = build_atlas(root)
    write_json(root / "processed" / "equation_atlas.json", atlas)
    write_json(root / "site" / "public" / "data" / "equation_atlas.json", atlas)
    write_pages(root, atlas)
    print(
        "Generated equation atlas with "
        f"{atlas['total_candidate_records']} candidates and "
        f"{atlas['reviewable_relation_count']} reviewable relations."
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
