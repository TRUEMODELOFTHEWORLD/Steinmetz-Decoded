def build_family_page(family_id: str, atlas: dict[str, Any]) -> str:
    family = FAMILIES.get(
        family_id,
        {
            "label": "General Equation Candidates",
            "why": "Candidates not yet classified into a stronger mathematical family.",
        },
    )
    records = [record for record in atlas["records"] if record.get("family") == family_id]
    records.sort(key=lambda item: (-int(item.get("quality_score") or 0), str(item.get("source_title")), int(item.get("line_start") or 999999999)))
