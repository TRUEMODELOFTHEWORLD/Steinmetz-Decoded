def load_json(path: Path, default: Any) -> Any:
    if not path.exists():
        return default
    return json.loads(path.read_text(encoding="utf-8"))


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def bib_key(value: str) -> str:
    allowed = "".join(ch if ch.isalnum() else "-" for ch in value.lower())
    parts = [part for part in allowed.split("-") if part]
    return "-".join(parts[:8]) or "steinmetz-source"


def bib_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return text.replace("\\", "\\\\").replace("{", "\\{").replace("}", "\\}")


def source_url(source: dict[str, Any]) -> str:
    archive_id = source.get("internet_archive_id")
    if archive_id:
        return f"https://archive.org/details/{archive_id}"
    local_raw = source.get("local_raw_file")
    if local_raw:
        return f"{REPO_URL}/blob/main/{local_raw}"
    return f"{SITE_URL}{source.get('site_path', '')}"


def citation_record_for_source(source: dict[str, Any]) -> dict[str, Any]:
    source_id = str(source["source_id"])
    year = source.get("year")
    return {
        "id": f"steinmetz-{source_id}",
        "type": "book" if "report" not in str(source.get("processed_status", "")).lower() else "report",
        "person": "Charles Proteus Steinmetz",
        "collection": "steinmetz",
        "title": source.get("title"),
        "author": [{"family": "Steinmetz", "given": "Charles Proteus"}],
        "issued": {"date-parts": [[year]]} if year else None,
        "year": year,
        "source_id": source_id,
        "source_status": source.get("source_status"),
        "processed_status": source.get("processed_status"),
        "internet_archive_id": source.get("internet_archive_id"),
        "local_raw_file": source.get("local_raw_file"),
        "site_url": f"{SITE_URL}{source.get('site_path', '')}",
        "source_url": source_url(source),
        "recommended_citation": f"Charles Proteus Steinmetz, {source.get('title')} ({year if year else 'n.d.'}). Steinmetz Decoded source record: {SITE_URL}{source.get('site_path', '')}",
        "verification_note": "Use this citation record as a source pointer. Exact quotations and page claims still require scan or edition verification.",
    }


def project_citation_record() -> dict[str, Any]:
    current_year = date.today().year
    return {
        "id": "steinmetz-decoded-project",
        "type": "webpage",
        "title": "Steinmetz Decoded",
        "author": [{"literal": "Steinmetz Decoded contributors"}],
        "issued": {"date-parts": [[current_year]]},
        "year": current_year,
        "site_url": SITE_URL,
        "source_url": REPO_URL,
        "recommended_citation": f"Steinmetz Decoded contributors, Steinmetz Decoded: A Source-Grounded Research Codex for Charles Proteus Steinmetz ({current_year}), {SITE_URL}",
        "verification_note": "Cite individual source pages for source claims; cite the project for the archive, tooling, commentary, and generated indexes.",
    }


def build_citation_index(root: Path) -> dict[str, Any]:
    source_catalog = load_json(root / "sources" / "source_catalog.json", [])
    if not isinstance(source_catalog, list):
        source_catalog = []
    records = [project_citation_record()]
    records.extend(citation_record_for_source(source) for source in source_catalog)
    return {
        "generated_at": date.today().isoformat(),
        "quality_note": "Citation records identify source custody and public archive pages. They are not replacements for exact page citations.",
        "records": records,
    }


def csl_record(record: dict[str, Any]) -> dict[str, Any]:
    issued = record.get("issued")
    out = {
        "id": record["id"],
        "type": record.get("type", "book"),
        "title": record.get("title"),
        "author": record.get("author", []),
        "URL": record.get("source_url") or record.get("site_url"),
        "note": record.get("verification_note"),
    }
    if issued:
        out["issued"] = issued
    return out


def build_bibtex(citations: dict[str, Any]) -> str:
    entries: list[str] = []
    for record in citations["records"]:
        entry_type = "misc" if record.get("type") == "webpage" else "book"
        key = bib_key(record["id"])
        year = record.get("year") or "n.d."
        author = "Steinmetz Decoded contributors"
        if record.get("person") == "Charles Proteus Steinmetz":
            author = "Steinmetz, Charles Proteus"
        fields = {
            "author": author,
            "title": record.get("title"),
            "year": year,
            "url": record.get("source_url") or record.get("site_url"),
            "note": record.get("verification_note"),
        }
        lines = [f"@{entry_type}{{{key},"]
        for name, value in fields.items():
            lines.append(f"  {name} = {{{bib_escape(value)}}},")
        lines.append("}")
        entries.append("\n".join(lines))
    return "\n\n".join(entries) + "\n"


def copy_public_data(root: Path) -> list[dict[str, Any]]:
    public_dir = root / "site" / "public" / "data"
    public_dir.mkdir(parents=True, exist_ok=True)
    exports: list[dict[str, Any]] = []
    for export_id, rel_path, description in DATA_EXPORTS:
        source_path = root / rel_path
        if not source_path.exists():
            continue
        output_name = f"{export_id}.json"
        target_path = public_dir / output_name
        shutil.copyfile(source_path, target_path)
        exports.append(
            {
                "id": export_id,
                "description": description,
                "source_path": rel_path.replace("\\", "/"),
                "public_url": f"{BASE_URL}/data/{output_name}",
                "format": "json",
            }
        )
    return exports


def write_public_manifest(root: Path, exports: list[dict[str, Any]]) -> None:
    manifest = {
        "generated_at": date.today().isoformat(),
        "project": "Steinmetz Decoded",
        "site_url": SITE_URL,
        "repository": REPO_URL,
        "quality_note": "Public data exports expose current review-state data. Candidate records require verification before citation as fact.",
        "exports": exports
        + [
            {
                "id": "citation_index_bibtex",
                "description": "BibTeX citation export for project and source records.",
                "source_path": "processed/citation_index.bib",
                "public_url": f"{BASE_URL}/data/citation_index.bib",
                "format": "bibtex",
            },
            {
                "id": "citation_index_csl",
                "description": "CSL JSON citation export for project and source records.",
                "source_path": "processed/citation_index.csl.json",
                "public_url": f"{BASE_URL}/data/citation_index.csl.json",
                "format": "csl-json",
            },
        ],
    }
    write_json(root / "site" / "public" / "data" / "manifest.json", manifest)


def md_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return text.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;").replace("|", "\\|")


def build_export_page(citations: dict[str, Any], exports: list[dict[str, Any]]) -> str:
    rows = []
    for export in exports:
        rows.append(
            f"| `{md_escape(export['id'])}` | {md_escape(export['description'])} | [{md_escape(export['format'])}]({export['public_url']}) | `{md_escape(export['source_path'])}` |"
        )
    rows.extend(
        [
            f"| `citation_index_bibtex` | BibTeX citation export for project and source records. | [bibtex]({BASE_URL}/data/citation_index.bib) | `processed/citation_index.bib` |",
            f"| `citation_index_csl` | CSL JSON citation export for project and source records. | [csl-json]({BASE_URL}/data/citation_index.csl.json) | `processed/citation_index.csl.json` |",
        ]
    )

    citation_rows = []
    for record in citations["records"]:
        citation_rows.append(
            f"| `{md_escape(record['id'])}` | {md_escape(record.get('title'))} | {md_escape(record.get('year'))} | [{md_escape('source')}]({md_escape(record.get('source_url') or record.get('site_url'))}) |"
        )

    return f"""---
title: "Citation And Data Export"
description: "Public citation records and reusable data exports for Steinmetz Decoded."
---

{GENERATED_NOTE}

## Citation And Data Export

The archive now publishes reusable data files for researchers, contributors, and external tools. These exports are designed for traceability: they expose source IDs, review states, generated indexes, and citation records without pretending candidate data is already canonical.

<div class="source-text-warning" data-layer="source">
  <strong>Use rule:</strong> cite generated data as an archive aid. Cite exact Steinmetz claims only after checking the source page, scan, and review status.
</div>

## Public Data Files

[Open the public data manifest]({BASE_URL}/data/manifest.json)

| Export | Description | Public file | Source path |
| --- | --- | --- | --- |
{chr(10).join(rows)}

## Citation Records

| Citation ID | Title | Year | Link |
| --- | --- | ---: | --- |
{chr(10).join(citation_rows)}

## Recommended Project Citation

```text
{citations['records'][0]['recommended_citation']}
```

## Review Boundary

Data exports preserve current review status. They should help researchers find and reuse the archive's work, but they do not remove the need for source verification, especially for equations, direct quotations, figure captions, patent claims, and interpretive comparisons.
"""


