def route_records(
    records: list[dict[str, Any]],
    themes: list[str],
    limit: int = 6,
    source_ids: list[str] | None = None,
    required_themes: list[str] | None = None,
    exclude_lanes: list[str] | None = None,
    preferred_sections: list[tuple[str, str]] | None = None,
) -> list[dict[str, Any]]:
    selected = []
    seen_sections = set()
    source_ids = source_ids or []
    required_themes = required_themes or []
    exclude_lanes = exclude_lanes or []
    preferred_sections = preferred_sections or []

    for preferred_source, preferred_slug in preferred_sections:
        for record in records:
            if record["section_id"] in seen_sections:
                continue
            if record["source_id"] == preferred_source and record["section_slug"] == preferred_slug:
                selected.append(record)
                seen_sections.add(record["section_id"])
                break
        if len(selected) >= limit:
            return selected

    for record in records:
        record_themes = record["themes"]
        if record["section_id"] in seen_sections:
            continue
        if source_ids and record["source_id"] not in source_ids:
            continue
        if record["lane"] in exclude_lanes:
            continue
        if required_themes and not all(theme in record_themes for theme in required_themes):
            continue
        if any(theme in record_themes[:4] for theme in themes):
            selected.append(record)
            seen_sections.add(record["section_id"])
        if len(selected) >= limit:
            break
    return selected


