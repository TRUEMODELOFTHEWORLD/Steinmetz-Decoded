def build_curriculum(records: list[dict[str, Any]]) -> str:
    levels = [
        ("Hour 1", ["radiation-light"], "Meet Steinmetz through readable radiation and light passages."),
        ("Day 1", ["symbolic-ac"], "Add the first AC symbolic-method passages."),
        ("Week 1", ["transients-waves-surges"], "Move into impedance, admittance, temporary terms, and oscillations."),
        ("Month 1", ["magnetism-hysteresis", "dielectric-capacity", "field-language"], "Build the deeper field, material, and energy-storage vocabulary."),
        ("Contributor Track", ["ether-and-relativity", "power-systems"], "Verify exact passages, promote figures/equations, and prepare review notes."),
    ]
    blocks = []
    for label, themes, why in levels:
        selected = route_records(
            records,
            themes,
            8,
            source_ids=["radiation-light-and-illumination", "general-lectures-electrical-engineering"]
            if label == "Hour 1"
            else None,
            required_themes=themes if label in {"Day 1", "Week 1"} else None,
            exclude_lanes=["ether-field-boundary"] if label == "Hour 1" else None,
            preferred_sections=[
                ("radiation-light-and-illumination", "lecture-01"),
                ("radiation-light-and-illumination", "lecture-06"),
                ("general-lectures-electrical-engineering", "lecture-07"),
            ]
            if label == "Hour 1"
            else None,
        )
        links = "\n".join(
            f"- [{md_escape(record['section_label'])}]({record['links']['source_text']}) - {md_escape(record['source_title'])}"
            for record in selected
        )
        blocks.append(f"## {label}\n\n{why}\n\n{links}\n")
    return f"""---
title: Steinmetz Study Curriculum
description: A staged source-first curriculum through the current Steinmetz corpus.
---

{GENERATED_NOTE}

This curriculum is a source-first path through the processed corpus. It is not a simplified replacement for the archive; it is a path through it.

{chr(10).join(blocks)}
"""


def write_pages(root: Path, records: list[dict[str, Any]], grouped: dict[str, list[dict[str, Any]]]) -> None:
    docs = root / "site" / "src" / "content" / "docs"
    atlas_dir = docs / "passage-atlas"
    if atlas_dir.exists():
        shutil.rmtree(atlas_dir)
    atlas_dir.mkdir(parents=True, exist_ok=True)
    write_text(atlas_dir / "index.mdx", build_atlas_index(records, grouped))
    for theme_id, theme in THEMES.items():
        write_text(atlas_dir / theme_id / "index.mdx", build_theme_page(theme_id, theme, grouped.get(theme_id, [])))
    write_text(docs / "hidden-gems" / "index.mdx", build_hidden_gems_page(records))
    research_map_dir = docs / "research-map"
    research_map_dir.mkdir(parents=True, exist_ok=True)
    write_text(research_map_dir / "index.mdx", build_research_map(records))
    write_text(research_map_dir / "study-curriculum.mdx", build_curriculum(records))


