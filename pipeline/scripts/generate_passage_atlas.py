def load_json(path: Path, default: Any) -> Any:
    if not path.exists():
        return default
    return json.loads(path.read_text(encoding="utf-8"))


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def slugify(value: str) -> str:
    value = value.lower()
    value = re.sub(r"[^a-z0-9]+", "-", value)
    value = value.strip("-")
    return value or "untitled"


def yaml_quote(value: Any) -> str:
    text = "" if value is None else str(value)
    text = text.replace("\\", "\\\\").replace('"', '\\"')
    return f'"{text}"'


def clean_text(value: Any, limit: int | None = None) -> str:
    text = "" if value is None else str(value)
    replacements = {
        "\u00a0": " ",
        "\u2013": "-",
        "\u2014": "-",
        "\u2018": "'",
        "\u2019": "'",
        "\u201c": '"',
        "\u201d": '"',
        "\ufeff": "",
    }
    for old, new in replacements.items():
        text = text.replace(old, new)
    text = re.sub(r"\s+", " ", text).strip()
    if limit and len(text) > limit:
        return text[: limit - 3].rstrip() + "..."
    return text


def md_escape(value: Any) -> str:
    return (
        clean_text(value)
        .replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace("{", "&#123;")
        .replace("}", "&#125;")
        .replace("|", "\\|")
    )


def html_escape(value: Any) -> str:
    return (
        clean_text(value)
        .replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def source_page_slug(record: dict[str, Any]) -> str:
    path = Path(str(record.get("text_path") or ""))
    return slugify(path.stem or str(record.get("id") or record.get("title") or "section"))


def source_text_url(source_id: str, record: dict[str, Any]) -> str:
    return f"{BASE_URL}/source-texts/{source_id}/{source_page_slug(record)}/"


def workbench_url(source_id: str, record: dict[str, Any]) -> str:
    return f"{BASE_URL}/chapter-workbench/{source_id}/{source_page_slug(record)}/"


def chapter_label(record: dict[str, Any]) -> str:
    kind = str(record.get("kind") or "section").replace("-", " ").title()
    number = record.get("number") or record.get("sequence")
    title = clean_text(record.get("title") or "Untitled")
    if number:
        return f"{kind} {number}: {title}"
    return f"{kind}: {title}"


def paragraph_blocks(text: str) -> list[tuple[int, int, str]]:
    blocks: list[tuple[int, int, str]] = []
    current: list[str] = []
    start_line = 1
    for index, line in enumerate(text.splitlines(), start=1):
        if line.strip():
            if not current:
                start_line = index
            current.append(line.strip())
        elif current:
            paragraph = clean_text(" ".join(current))
            blocks.append((start_line, index - 1, paragraph))
            current = []
    if current:
        paragraph = clean_text(" ".join(current))
        blocks.append((start_line, len(text.splitlines()), paragraph))
    return blocks


def theme_hits(text: str) -> dict[str, int]:
    lower = f" {text.lower()} "
    counts: dict[str, int] = {}
    for theme_id, theme in THEMES.items():
        count = 0
        for pattern in theme["patterns"]:
            pattern_lower = pattern.lower()
            count += lower.count(pattern_lower)
            if "-" in pattern_lower:
                count += lower.count(pattern_lower.replace("-", " "))
        if count:
            counts[theme_id] = count
    return counts


def signal_hits(text: str) -> list[str]:
    hits = []
    for label, pattern, _weight in SIGNAL_PATTERNS:
        if pattern.search(text):
            hits.append(label)
    return hits


def score_passage(text: str, themes: dict[str, int], signals: list[str]) -> int:
    word_count = len(re.findall(r"[A-Za-z][A-Za-z'-]*", text))
    if word_count < 45 or word_count > 260:
        return 0
    score = 0
    for theme_id, count in themes.items():
        score += min(count, 10) * (16 if theme_id in {"ether-and-relativity", "field-language"} else 12)
    for label, _pattern, weight in SIGNAL_PATTERNS:
        if label in signals:
            score += weight
    if len(themes) >= 2:
        score += 18
    if len(themes) >= 3:
        score += 20
    if 70 <= word_count <= 170:
        score += 12
    return score


def lane_for(themes: dict[str, int], signals: list[str]) -> str:
    ranked = [theme for theme, _count in Counter(themes).most_common()]
    if "ether-and-relativity" in themes:
        return "ether-field-boundary"
    if "symbolic-ac" in themes:
        return "mathematical-language"
    if "transients-waves-surges" in themes:
        return "transient-and-wave-reasoning"
    if "field-language" in themes or "dielectric-capacity" in themes:
        return "field-language"
    if "magnetism-hysteresis" in themes:
        return "magnetic-material-language"
    if "definition" in signals or "distinction" in signals:
        return "definition-or-distinction"
    return ranked[0] if ranked else "general-reader-gem"


def why_review(themes: dict[str, int], lane: str) -> str:
    if not themes:
        return "Candidate passage selected for editorial review; verify against scan before promotion."
    top_theme = max(themes, key=themes.get)
    theme = THEMES[top_theme]
    return str(theme.get("why") or f"Candidate passage for the {lane} lane.")


def collect_passages(root: Path) -> list[dict[str, Any]]:
    catalog = load_json(root / "sources" / "source_catalog.json", [])
    if not isinstance(catalog, list):
        return []

    records: list[dict[str, Any]] = []
    for source in catalog:
        source_id = str(source.get("source_id") or "")
        if not source_id:
            continue
        chapters = load_json(root / "processed" / source_id / "chapters.json", [])
        if not isinstance(chapters, list):
            continue
        for chapter in chapters:
            if not isinstance(chapter, dict):
                continue
            text_path = root / str(chapter.get("text_path") or "")
            if not text_path.exists():
                continue
            text = text_path.read_text(encoding="utf-8", errors="replace")
            for local_start, local_end, paragraph in paragraph_blocks(text):
                themes = theme_hits(paragraph)
                if not themes:
                    continue
                signals = signal_hits(paragraph)
                score = score_passage(paragraph, themes, signals)
                if score <= 0:
                    continue
                source_line_start = int(chapter.get("line_start") or 0) + local_start - 1
                source_line_end = int(chapter.get("line_start") or 0) + local_end - 1
                lane = lane_for(themes, signals)
                record_id = f"{source_id}-{source_page_slug(chapter)}-passage-{local_start}"
                records.append(
                    {
                        "id": record_id,
                        "source_id": source_id,
                        "source_title": source.get("title"),
                        "year": source.get("year"),
                        "section_id": chapter.get("id"),
                        "section_label": chapter_label(chapter),
                        "section_slug": source_page_slug(chapter),
                        "line_start": source_line_start,
                        "line_end": source_line_end,
                        "local_line_start": local_start,
                        "local_line_end": local_end,
                        "score": score,
                        "lane": lane,
                        "themes": sorted(themes, key=lambda item: (-themes[item], item)),
                        "theme_counts": themes,
                        "signals": signals,
                        "passage": clean_text(paragraph, 920),
                        "why_review": why_review(themes, lane),
                        "status": "ocr-derived-passage-candidate",
                        "verification": "needs scan verification before exact quotation",
                        "links": {
                            "source_text": source_text_url(source_id, chapter),
                            "workbench": workbench_url(source_id, chapter),
                            "source_overview": f"{BASE_URL}/sources/{source_id}/",
                            "book_coverage": f"{BASE_URL}/book-coverage/{source_id}/",
                        },
                    }
                )
    records.sort(key=lambda item: (-int(item["score"]), str(item["source_title"]), str(item["section_label"])))
    return records


def balanced_subset(records: list[dict[str, Any]], limit_per_source: int = 10, global_limit: int = 180) -> list[dict[str, Any]]:
    counts: Counter[str] = Counter()
    selected: list[dict[str, Any]] = []
    for record in records:
        source_id = str(record["source_id"])
        if counts[source_id] >= limit_per_source:
            continue
        selected.append(record)
        counts[source_id] += 1
        if len(selected) >= global_limit:
            break
    return selected


def theme_subsets(records: list[dict[str, Any]], limit: int = 36) -> dict[str, list[dict[str, Any]]]:
    grouped: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for record in records:
        for theme in record["themes"]:
            if len(grouped[theme]) < limit:
                grouped[theme].append(record)
    return dict(grouped)


