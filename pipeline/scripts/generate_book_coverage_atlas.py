def build_atlas(root: Path) -> dict[str, Any]:
    catalog = load_json(root / "sources" / "source_catalog.json", [])
    workbench = load_json(root / "processed" / "chapter_workbench.json", {})
    records = workbench.get("records", []) if isinstance(workbench, dict) else []
    grouped: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for record in records:
        grouped[str(record.get("source_id"))].append(record)

    sources = []
    for source in catalog:
        source_id = str(source.get("source_id") or "")
        if not source_id:
            continue
        source_records = grouped.get(source_id, [])
        sources.append(aggregate_records(source, source_records))

    return {
        "generated_at": date.today().isoformat(),
        "quality_note": "Book coverage atlas exposes candidate section-level representation. It does not certify OCR text, equations, figures, or quotations as scan-verified.",
        "source_count": len(sources),
        "total_sections": sum(source["section_count"] for source in sources),
        "sources": sources,
    }


def main() -> None:
    root = Path(__file__).resolve().parents[2]
    atlas = build_atlas(root)
    write_json(root / "processed" / "book_coverage_atlas.json", atlas)
    write_index_page(root, atlas)
    for source in atlas["sources"]:
        write_source_page(root, source)


if __name__ == "__main__":
    main()
