#!/usr/bin/env python3
"""Generate book-level coverage atlas pages from chapter workbench records.

The source-text browser and chapter workbench are intentionally granular. This
script adds the missing book-level view: for each source, it summarizes every
processed section and exposes the direct path into source text, research
workbench, concepts, candidate equations, figures, and quotes.
"""

from __future__ import annotations

import json
import re
from collections import Counter, defaultdict
from datetime import date
from pathlib import Path
from typing import Any
from urllib.parse import quote


BASE_URL = ""
GENERATED_NOTE = "{/* Generated by pipeline/scripts/generate_book_coverage_atlas.py. Do not edit by hand. */}"

THEME_LABELS = {
    "alternating-current": "Alternating current",
    "complex-quantities": "Complex quantities",
    "dielectricity": "Dielectricity / capacity",
    "ether": "Ether references",
    "fields": "Field language",
    "hysteresis": "Hysteresis",
    "impedance-reactance": "Impedance / reactance",
    "lightning-surges": "Lightning / surges",
    "magnetism": "Magnetism",
    "radiation-light": "Radiation / light",
    "transients": "Transients / damping",
    "waves-lines": "Waves / transmission lines",
}


def load_json(path: Path, default: Any) -> Any:
    if not path.exists():
        return default
    return json.loads(path.read_text(encoding="utf-8"))


def write_json(path: Path, data: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def yaml_quote(value: Any) -> str:
    text = "" if value is None else str(value)
    return '"' + text.replace("\\", "\\\\").replace('"', '\\"') + '"'


def md_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace("{", "&#123;")
        .replace("}", "&#125;")
        .replace("$", "&#36;")
        .replace("|", "\\|")
        .replace("\n", " ")
    )


def html_escape(value: Any) -> str:
    text = "" if value is None else str(value)
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def site_link(path: str | None) -> str:
    if not path:
        return ""
    if path.startswith("http"):
        return path
    if BASE_URL and path.startswith(BASE_URL):
        return path
    if path.startswith("/"):
        return f"{BASE_URL}{path}"
    return f"{BASE_URL}/{path}"


def slugify(value: Any) -> str:
    text = str(value or "").strip().lower()
    text = re.sub(r"[^a-z0-9]+", "-", text)
    return text.strip("-")


def chip_href(item: dict[str, Any], kind: str) -> str:
    slug = slugify(item.get("id") or item.get("label"))
    if not slug:
        return ""
    if kind == "theme":
        return f"{BASE_URL}/theme-evidence/#{slug}"
    if kind == "concept":
        return f"{BASE_URL}/concept-concordance/{slug}/"
    if kind == "glossary":
        return f"{BASE_URL}/glossary/#{slug}"
    return ""


def focused_reader_url(url: str | None, term: str | None = None) -> str:
    if not url:
        return ""
    if not term:
        return url
    separator = "&" if "?" in url else "?"
    return f"{url}{separator}q={quote(term)}#source-text-reader"


def archive_url(source: dict[str, Any]) -> str:
    archive_id = source.get("internet_archive_id")
    if archive_id:
        return f"https://archive.org/details/{archive_id}"
    local_raw = source.get("local_raw_file")
    if local_raw:
        return f"https://github.com/TRUEMODELOFTHEWORLD/Steinmetz-Decoded/blob/main/{local_raw}"
    return site_link(source.get("site_path"))


def label_for(record: dict[str, Any]) -> str:
    return str(record.get("label") or record.get("title") or record.get("id") or "Untitled section")


def theme_label(theme: str) -> str:
    return THEME_LABELS.get(theme, theme.replace("-", " ").title())


def format_number(value: int) -> str:
    return f"{value:,}"


def sum_counter(counter: Counter[str], limit: int = 8) -> list[dict[str, Any]]:
    return [
        {
            "id": key,
            "label": theme_label(key),
            "count": count,
        }
        for key, count in counter.most_common(limit)
    ]


def aggregate_records(source: dict[str, Any], records: list[dict[str, Any]]) -> dict[str, Any]:
    theme_counter: Counter[str] = Counter()
    concept_counter: Counter[str] = Counter()
    glossary_counter: Counter[str] = Counter()
    concept_labels: dict[str, str] = {}
    glossary_labels: dict[str, str] = {}

    word_total = 0
    equation_total = 0
    figure_total = 0
    quote_total = 0
    section_rows: list[dict[str, Any]] = []

    for record in records:
        word_count = int(record.get("word_count") or 0)
        word_total += word_count
        equation_count = len(record.get("equations") or [])
        figure_count = len(record.get("figures") or [])
        quote_count = len(record.get("quotes") or [])
        equation_total += equation_count
        figure_total += figure_count
        quote_total += quote_count

        for theme, count in (record.get("theme_counts") or {}).items():
            theme_counter[str(theme)] += int(count or 0)
        for hit in record.get("concept_hits") or []:
            concept_id = str(hit.get("id") or hit.get("label") or "")
            if not concept_id:
                continue
            concept_counter[concept_id] += int(hit.get("count") or 0)
            concept_labels[concept_id] = str(hit.get("label") or concept_id)
        for hit in record.get("glossary_hits") or []:
            term_id = str(hit.get("id") or hit.get("term") or hit.get("label") or "")
            if not term_id:
                continue
            glossary_counter[term_id] += int(hit.get("count") or 0)
            glossary_labels[term_id] = str(hit.get("label") or hit.get("term") or term_id)

        links = record.get("links") or {}
        section_rows.append(
            {
                "id": record.get("id"),
                "label": label_for(record),
                "slug": record.get("slug"),
                "location": record.get("location"),
                "status": record.get("status"),
                "word_count": word_count,
                "top_themes": [theme_label(str(theme)) for theme in (record.get("top_themes") or [])[:4]],
                "top_concepts": [
                    {
                        "id": hit.get("id"),
                        "label": hit.get("label"),
                        "count": hit.get("count"),
                    }
                    for hit in (record.get("concept_hits") or [])[:5]
                ],
                "equation_count": equation_count,
                "figure_count": figure_count,
                "quote_count": quote_count,
                "links": {
                    "source_text": site_link(links.get("source_text") or f"/source-texts/{source['source_id']}/{record.get('slug')}/"),
                    "workbench": site_link(f"/chapter-workbench/{source['source_id']}/{record.get('slug')}/"),
                    "asset": links.get("asset"),
                    "github_text": links.get("github_text"),
                },
            }
        )

    top_concepts = [
        {"id": key, "label": concept_labels.get(key, key), "count": count}
        for key, count in concept_counter.most_common(12)
    ]
    top_glossary = [
        {"id": key, "label": glossary_labels.get(key, key), "count": count}
        for key, count in glossary_counter.most_common(12)
    ]

    priority_sections = sorted(
        section_rows,
        key=lambda item: (
            item["equation_count"] * 120
            + item["figure_count"] * 90
            + item["quote_count"] * 70
            + len(item["top_concepts"]) * 45
            + item["word_count"] // 80
        ),
        reverse=True,
    )[:14]

    result = {
        "source_id": source["source_id"],
        "title": source.get("title"),
        "year": source.get("year"),
        "source_status": source.get("source_status"),
        "processed_status": source.get("processed_status"),
        "internet_archive_id": source.get("internet_archive_id"),
        "local_raw_file": source.get("local_raw_file"),
        "site_path": source.get("site_path"),
        "links": {
            "coverage": f"{BASE_URL}/book-coverage/{source['source_id']}/",
            "curated_source": site_link(source.get("site_path")),
            "source_text_index": f"{BASE_URL}/source-texts/{source['source_id']}/",
            "chapter_workbench_index": f"{BASE_URL}/chapter-workbench/{source['source_id']}/",
            "archive": archive_url(source),
        },
        "section_count": len(section_rows),
        "word_total": word_total,
        "equation_candidate_count": equation_total,
        "figure_candidate_count": figure_total,
        "quote_candidate_count": quote_total,
        "theme_totals": sum_counter(theme_counter, 12),
        "top_concepts": top_concepts,
        "top_glossary": top_glossary,
        "priority_sections": priority_sections,
        "sections": section_rows,
    }
    result["study_guide"] = build_study_guide(result)
    return result


def compact_section_ref(section: dict[str, Any]) -> dict[str, Any]:
    return {
        "id": section.get("id"),
        "label": section.get("label"),
        "slug": section.get("slug"),
        "word_count": section.get("word_count"),
        "top_themes": (section.get("top_themes") or [])[:4],
        "top_concepts": (section.get("top_concepts") or [])[:4],
        "equation_count": section.get("equation_count", 0),
        "figure_count": section.get("figure_count", 0),
        "quote_count": section.get("quote_count", 0),
        "links": section.get("links") or {},
    }


def section_signal_score(section: dict[str, Any]) -> int:
    return (
        int(section.get("equation_count") or 0) * 80
        + int(section.get("figure_count") or 0) * 70
        + int(section.get("quote_count") or 0) * 65
        + len(section.get("top_concepts") or []) * 35
        + int(section.get("word_count") or 0) // 150
    )


def top_sections_by(source: dict[str, Any], key: str, limit: int = 4) -> list[dict[str, Any]]:
    sections = [
        section for section in source.get("sections", []) if int(section.get(key) or 0) > 0
    ]
    sections.sort(
        key=lambda section: (
            -int(section.get(key) or 0),
            -section_signal_score(section),
            str(section.get("label") or ""),
        )
    )
    return [compact_section_ref(section) for section in sections[:limit]]


def theme_sections(source: dict[str, Any], terms: list[str], limit: int = 4) -> list[dict[str, Any]]:
    wanted = [term.lower() for term in terms]
    sections: list[dict[str, Any]] = []
    for section in source.get("sections", []):
        haystack = " ".join(section.get("top_themes") or [])
        haystack += " " + " ".join(hit.get("label", "") for hit in section.get("top_concepts", []))
        haystack = haystack.lower()
        if any(term in haystack for term in wanted):
            sections.append(section)
    sections.sort(key=lambda section: (-section_signal_score(section), str(section.get("label") or "")))
    return [compact_section_ref(section) for section in sections[:limit]]


def build_study_guide(source: dict[str, Any]) -> dict[str, Any]:
    first_read = [compact_section_ref(section) for section in (source.get("priority_sections") or [])[:5]]
    math_sections = top_sections_by(source, "equation_count", 5)
    visual_sections = top_sections_by(source, "figure_count", 5)
    field_language_sections = theme_sections(
        source,
        ["field", "ether", "dielectric", "capacity", "magnetism", "hysteresis", "force"],
        5,
    )
    glossary_sections = theme_sections(
        source,
        ["reactance", "impedance", "admittance", "conductance", "susceptance", "capacity", "counter"],
        5,
    )
    return {
        "quality_note": "Generated from section-level candidate metadata. Use as a reading guide, not as scan verification.",
        "first_read": first_read,
        "mathematics": math_sections,
        "visuals": visual_sections,
        "field_language": field_language_sections,
        "terminology": glossary_sections,
    }


